# Perception & robustness layer (read-only diagnostics)
# ═══════════════════════════════════════════════════════════════════════════
# These functions compute diagnostic fields AFTER all pattern resolution is
# complete.  They are purely additive — they never modify authoritative_pattern,
# pattern_candidates, or lighting_intel.  Any failure leaves the field as None.

_CUE_FIELD_NAMES = [
    "shadow_edge_hardness", "primary_shadow_direction", "vertical_light_angle",
    "catchlight_position", "catchlight_shape", "catchlight_topology",
    "highlight_axis_map", "highlight_symmetry", "continuous_source_signals",
    "bounce_contributor", "separation_light", "off_axis_key",
    "light_structure", "highlight_to_shadow_transition", "contrast_ratio",
    "subject_background_separation", "background_illumination",
    "specular_highlight_behavior", "reflection_architecture",
    "multi_shadow_detection", "environmental_shadow_continuity",
    "pose_induced_shadow_interference", "tonal_processing_estimation",
    "shadow_interruption_pattern",
]

_FACE_DEPENDENT_CUES = {
    "primary_shadow_direction", "vertical_light_angle",
    "pose_induced_shadow_interference", "shadow_interruption_pattern",
    "light_structure",
}


def _compute_face_validation(result: "AnalysisResult") -> FaceValidation:
    """Assess face detection quality from vision_data."""
    fv = FaceValidation()

    face_box = result.debug_data.get("face_box") if result.debug_data else None
    fv.face_detected = face_box is not None

    # Face yaw from catchlight pipeline
    cl_info = result.vision_data.get("catchlights", {}) if result.vision_data else {}
    fv.face_yaw = cl_info.get("face_yaw")

    # Detection score from MediaPipe
    ra = result.vision_data.get("region_attribution", {}) if result.vision_data else {}
    score = ra.get("face_detection_score")
    fv.face_confidence = score if score is not None else (0.8 if fv.face_detected else 0.0)

    # Face area ratio
    if face_box is not None:
        fb = face_box if not isinstance(face_box, list) else tuple(face_box)
        x0, y0, x1, y1 = fb
        face_area = max(0, (x1 - x0)) * max(0, (y1 - y0))
        # Get image dimensions from region_attribution masks or vision_data
        img_h = ra.get("_image_h", 0)
        img_w = ra.get("_image_w", 0)
        if img_h == 0 or img_w == 0:
            # Fallback: estimate from face_box bounds
            img_h = max(y1, 1)
            img_w = max(x1, 1)
        img_area = img_h * img_w
        fv.face_box_area_ratio = round(face_area / img_area, 4) if img_area > 0 else 0.0

    # Quality assessment
    if fv.face_detected and fv.face_box_area_ratio > 0.02 and fv.face_yaw is not None:
        fv.face_quality = "good"
    elif fv.face_detected:
        fv.face_quality = "partial"
    else:
        fv.face_quality = "none"

    return fv


def _compute_signal_reliability(result: "AnalysisResult") -> SignalReliability:
    """Assess signal extraction completeness and confidence."""
    sr = SignalReliability()
    sr.signals_total = len(_CUE_FIELD_NAMES)

    cr = result.cue_report
    if cr is None:
        sr.missing_signals = list(_CUE_FIELD_NAMES)
        return sr

    for name in _CUE_FIELD_NAMES:
        cue = getattr(cr, name, None)
        if cue is None:
            sr.missing_signals.append(name)
        else:
            sr.signals_available += 1
            if name in _FACE_DEPENDENT_CUES:
                conf = getattr(cue, "confidence", 0.0)
                if conf > 0:
                    sr.face_dependent_signals_available += 1
            conf = getattr(cue, "confidence", None)
            if conf is not None and conf < 0.3:
                sr.weak_signals.append(name)

    sr.overall_signal_strength = round(cr.overall_confidence(), 3) if hasattr(cr, "overall_confidence") else 0.0
    return sr


def _compute_edge_case_flags(
    result: "AnalysisResult", fv: FaceValidation,
) -> EdgeCaseFlags:
    """Detect edge cases from existing signals (no new classifiers)."""
    ecf = EdgeCaseFlags()
    ecf.no_face = not fv.face_detected

    cr = result.cue_report
    cls = result.classification or {}

    if cr is None:
        return ecf

    # B&W processing
    tpe = getattr(cr, "tonal_processing_estimation", None)
    if tpe and getattr(tpe, "is_bw", False):
        ecf.bw_processing = True

    # Blown highlights — use actual pixel-level clipping detection from the
    # tonal processing pass (histogram p99 pegged at white with p99-p95 < 3).
    # The previous contrast_ratio > 8.0 proxy false-fired on any dramatic
    # Rembrandt/Loop portrait: deep shadows + bright skin naturally push
    # the p95/p5 ratio above 8 without a single clipped highlight pixel.
    if tpe is not None and getattr(tpe, "highlights_clipped", False):
        ecf.blown_highlights = True

    # Environment hints
    esc = getattr(cr, "environmental_shadow_continuity", None)
    hints = getattr(esc, "environment_hints", []) if esc else []

    # Mixed color temperature: both warm and cool hints present
    has_warm = any(h.startswith("warm") for h in hints)
    has_cool = any(h.startswith("cool") for h in hints)
    if has_warm and has_cool:
        ecf.mixed_color_temperature = True

    # Outdoor foliage shadows
    if "dappled_foliage" in hints:
        ecf.outdoor_foliage_shadows = True

    # Window light gradient
    bg_ill = getattr(cr, "background_illumination", None)
    bg_pattern = getattr(bg_ill, "pattern", "") if bg_ill else ""
    has_natural = getattr(esc, "has_natural_indicators", False) if esc else False
    if bg_pattern == "gradient" and has_natural:
        ecf.window_light_gradient = True

    # Extreme low-key
    brightness = (cls.get("brightness") or "").lower()
    ls = getattr(cr, "light_structure", None)
    sd = getattr(ls, "shadow_density", 0.0) if ls else 0.0
    if brightness == "low" and sd > 0.5:
        ecf.extreme_low_key = True

    # Earring / jewellery catchlight contamination — surfaced from lighting_inference
    li = getattr(result, "lighting_intel", None)
    if li is not None and getattr(li, "earring_catchlight_contamination", False):
        ecf.earring_catchlight_contamination = True

    return ecf


def _compute_perception_explanation(
    result: "AnalysisResult", fv: FaceValidation, sr: SignalReliability,
) -> PerceptionExplanation:
    """Build structured explanation of pattern selection."""
    pe = PerceptionExplanation()
    cr = result.cue_report
    pc = result.pattern_candidates
    pattern = result.authoritative_pattern
    source = result.authoritative_pattern_source

    # ── Supporting signals ───────────────────────────────────────────
    supporting = []
    if cr is not None:
        ls = getattr(cr, "light_structure", None)
        if ls and getattr(ls, "pattern_name", "unknown") != "unknown":
            supporting.append({
                "signal": "light_structure",
                "value": ls.pattern_name,
                "confidence": round(getattr(ls, "confidence", 0.0), 3),
            })

        psd = getattr(cr, "primary_shadow_direction", None)
        if psd and getattr(psd, "direction", "unknown") != "unknown":
            supporting.append({
                "signal": "primary_shadow_direction",
                "value": psd.direction,
                "confidence": round(getattr(psd, "confidence", 0.0), 3),
            })

        cs = getattr(cr, "catchlight_shape", None)
        if cs and getattr(cs, "dominant_shape", "unknown") != "unknown":
            supporting.append({
                "signal": "catchlight_shape",
                "value": cs.dominant_shape,
                "confidence": round(getattr(cs, "confidence", 0.0), 3),
            })

        esc = getattr(cr, "environmental_shadow_continuity", None)
        if esc and getattr(esc, "confidence", 0.0) > 0.3:
            hints = getattr(esc, "environment_hints", [])
            if hints:
                supporting.append({
                    "signal": "environmental_shadow_continuity",
                    "value": ", ".join(hints[:3]),
                    "confidence": round(esc.confidence, 3),
                })

        seh = getattr(cr, "shadow_edge_hardness", None)
        if seh and getattr(seh, "classification", "unknown") != "unknown":
            supporting.append({
                "signal": "shadow_edge_hardness",
                "value": seh.classification,
                "confidence": round(getattr(seh, "confidence", 0.0), 3),
            })

    # Sort by confidence desc, cap at 5
    supporting.sort(key=lambda s: s["confidence"], reverse=True)
    pe.supporting_signals = supporting[:5]

    # ── Contradicting signals ────────────────────────────────────────
    contradicting = []
    if pc and pc.contradictions:
        for c in pc.contradictions:
            contradicting.append({"signal": "classifier_disagreement", "value": c, "confidence": 0.0})

    # Light structure pattern differs from authoritative
    if cr is not None:
        ls = getattr(cr, "light_structure", None)
        ls_pat = getattr(ls, "pattern_name", "unknown") if ls else "unknown"
        if ls_pat != "unknown" and ls_pat != pattern:
            contradicting.append({
                "signal": "light_structure",
                "value": f"detected '{ls_pat}' but authoritative is '{pattern}'",
                "confidence": round(getattr(ls, "confidence", 0.0), 3),
            })

    pe.contradicting_signals = contradicting

    # ── Ambiguity flags ──────────────────────────────────────────────
    flags = []
    if not fv.face_detected:
        flags.append("no_face_detected")
    if fv.face_box_area_ratio > 0 and fv.face_box_area_ratio < 0.01:
        flags.append("tiny_face")
    if pc and pc.alternates:
        primary_conf = pc.primary.confidence if pc.primary else 0.0
        for alt in pc.alternates:
            if abs(alt.confidence - primary_conf) < 0.15:
                flags.append("multiple_patterns_close_confidence")
                break
    if cr is not None:
        tpe = getattr(cr, "tonal_processing_estimation", None)
        if tpe and getattr(tpe, "is_bw", False):
            flags.append("bw_limits_color_cues")
    if sr.signals_available < 10:
        flags.append("low_signal_count")
    if "golden_hour_geometry_fallback" in (result.notes or []):
        flags.append("golden_hour_geometry_fallback")

    pe.ambiguity_flags = flags

    # ── Pattern reasoning ────────────────────────────────────────────
    conf = 0.0
    if pc and pc.primary:
        conf = pc.primary.confidence
    detail = ""
    if pe.supporting_signals:
        top = pe.supporting_signals[0]
        detail = f"{top['signal'].replace('_', ' ')} indicates {top['value']}."
    pe.pattern_reasoning = (
        f"Pattern '{pattern}' selected by {source} "
        f"(confidence {conf:.0%}). {detail}"
    ).strip()

    return pe


def _compute_perception_layer(result: "AnalysisResult") -> None:
    """Compute all perception/robustness diagnostics.

    Called after all pattern resolution is complete.  This function is
    purely read-only — it never modifies authoritative_pattern,
    pattern_candidates, or lighting_intel.  Any sub-computation failure
    leaves the corresponding field as None.
    """
    try:
        fv = _compute_face_validation(result)
        result.face_validation = fv
    except Exception:
        fv = FaceValidation()
        logger.debug("Face validation computation failed", exc_info=True)

    try:
        sr = _compute_signal_reliability(result)
        result.signal_reliability = sr
    except Exception:
        sr = SignalReliability()
        logger.debug("Signal reliability computation failed", exc_info=True)

    try:
        result.edge_case_flags = _compute_edge_case_flags(result, fv)
    except Exception:
        logger.debug("Edge case flags computation failed", exc_info=True)

    try:
        result.perception_explanation = _compute_perception_explanation(result, fv, sr)
    except Exception:
        logger.debug("Perception explanation computation failed", exc_info=True)


# ═══════════════════════════════════════════════════════════════════════════
# Expert Deconstruction Order — Layer 0: Mode Pre-Read
# ═══════════════════════════════════════════════════════════════════════════
#
# Runs AFTER describe_image (Step 1) so face detection, tonal processing,
# and classification are available.  Runs BEFORE any pattern inference.
#
# Populates result.mode_flags:
#   no_face    — face mesh unavailable; face-geometry patterns are invalid
#   is_bw      — B&W image; color-direction signals are disabled
#   is_hcg     — high-contrast grade; shadow-geometry thresholds need relaxation
#   scene_type — "portrait" | "product" | "no_subject"
#
# All downstream layers inspect result.mode_flags to gate or adapt logic.
# Nothing in this function produces a pattern name.

def _layer0_mode_preread(result: "AnalysisResult") -> None:
    """Populate result.mode_flags from Step 1 signals.

    Expert Deconstruction Order Stage 0: mode pre-read.
    Must run before extended pipeline and all pattern inference so that
    B&W guards, HCG threshold relaxations, and no-face gating are available
    to every downstream stage.
    """
    flags: Dict[str, Any] = {}
    cr = result.cue_report

    # ── B&W / HCG flags from tonal_processing ──────────────────────────────
    tp = getattr(cr, "tonal_processing", None) if cr else None
    flags["is_bw"]  = bool(getattr(tp, "is_bw", False))
    flags["is_hcg"] = bool(getattr(tp, "is_high_contrast_grade", False))

    # ── Face detection ──────────────────────────────────────────────────────
    # Priority order: face_box (most definitive) > vision_data has_face flag
    # > cue_report no_face_mesh inversion (least definitive).
    _face_box = (result.debug_data or {}).get("face_box")
    _has_face = _face_box is not None
    if not _has_face and result.vision_data:
        _vd_cl = result.vision_data.get("catchlights", {})
        if isinstance(_vd_cl, dict) and _vd_cl.get("has_face"):
            _has_face = True
    if not _has_face and cr:
        # no_face_mesh == True means face mesh failed → infer no face
        _no_mesh = getattr(cr, "no_face_mesh", False)
        if not _no_mesh:
            # mesh didn't fail — face was likely present
            _has_face = True
    flags["no_face"] = not _has_face

    # ── Scene type ──────────────────────────────────────────────────────────
    # Derived from classification first; inferred from face presence when absent.
    # "portrait" requires a detected face — any portrait-geometry pattern
    # (loop, rembrandt, butterfly…) is only valid when no_face is False.
    _cls = result.classification or {}
    _scene = _cls.get("scene_type") or _cls.get("subject_type") or ""
    if not _scene:
        if _has_face:
            _scene = "portrait"
        elif any(k in str(_cls).lower() for k in ("product", "tabletop", "bottle", "object")):
            _scene = "product"
        else:
            _scene = "no_subject"
    flags["scene_type"] = _scene

    result.mode_flags = flags
    logger.debug(
        "[layer0] no_face=%s is_bw=%s is_hcg=%s scene_type=%s",
        flags["no_face"], flags["is_bw"], flags["is_hcg"], flags["scene_type"],
    )


# ═══════════════════════════════════════════════════════════════════════════
# Expert Deconstruction Order — Stage 1: Definitive Signature Check
# ═══════════════════════════════════════════════════════════════════════════
#
# Runs AFTER the extended pipeline (Layer 1 CV signals complete) but BEFORE
# lighting_inference and cue_inference (Layer 2 geometry trees).
#
# If a single feature IDENTIFIES the pattern BEYOND DOUBT — ring-light donut
# catchlights, hard backlight silhouette — the pattern is set here and the
# Layer 2/3/5 inference trees are bypassed.  The resolver (Layer 6) treats
# a definitive_pattern as priority 0 above reference_read.
#
# Definitive signatures (short-circuit conditions):
#   ring_light     — bilateral donut catchlights (hollow ring morphology)
#   triangle       — Hurley triangle: bilateral UL+UR+LO with handedness check
#   silhouette_key — hard backlight + no catchlights + face fully in shadow
#
# Confidence is implicitly 0.95 when a definitive signature fires.
# These signatures CANNOT be confused with any other pattern when confirmed.
#
# ENFORCEMENT BOUND (analysis-order doctrine): this stage must NEVER short-
# circuit on generic modifier guesses ("looks like a softbox", "round
# catchlight => beauty dish"), tonal guesses ("bright => high_key"), or
# source_context guesses ("warm => golden_hour").  Those inputs are confidence-
# shaping only and belong to later stages (7-12).  Adding any new branch to
# this function requires a physically unique signal that CANNOT be produced
# by any other pattern -- not a probabilistic heuristic.

def _stage1_definitive_signatures(result: "AnalysisResult") -> Optional[str]:
    """Check for patterns identifiable from a single definitive signal.

    Returns the canonical pattern name if found, or None to continue with
    the full Layer 2 analysis tree.

    Expert Deconstruction Order Stage 1.
    """
    cr = result.cue_report
    mode = result.mode_flags

    # ── Ring light: bilateral donut catchlights ─────────────────────────────
    # Donut (annular / hollow-ring) morphology is physically unique to ring
    # flash and ring LED sources.  No other modifier produces a dark center
    # in the catchlight reflection.  Both eyes must show the ring shape to
    # rule out single-eye reflections from glasses frames or jewelry.
    #
    # Guard: natural light cannot produce ring catchlights — the signature
    # only fires in controlled / studio lighting contexts.
    _has_ring = False
    _cs = getattr(cr, "catchlight_shape", None) if cr else None
    if _cs is not None:
        _cs_seen  = getattr(_cs, "shapes_seen", []) or []
        _cs_shape = getattr(_cs, "dominant_shape", "") or ""
        if _cs_shape == "ring" or "ring" in _cs_seen:
            _has_ring = True

    # Bilateral confirmation from raw CV data
    if _has_ring and result.vision_data:
        _vd_cl   = result.vision_data.get("catchlights", {})
        _cv_cls  = (_vd_cl.get("catchlights", []) if isinstance(_vd_cl, dict) else [])
        _ring_eyes = sum(1 for c in _cv_cls if isinstance(c, dict) and c.get("shape") == "ring")
        if _ring_eyes < 2:
            # Unilateral ring — jewelry / glasses / single-eye glint.
            # Not a definitive ring_light signature; let Layer 2 handle it.
            _has_ring = False

    # Natural light guard
    if _has_ring:
        _ci_env = (result.cue_inference_result or {}).get("environment")
        if getattr(_ci_env, "is_natural_light", False):
            _has_ring = False

    if _has_ring:
        logger.info("[stage1] ring_light — bilateral donut catchlights confirmed")
        return "ring_light"

    # ── Hurley triangle: bilateral upper-left + upper-right + lower catchlights ─
    # The Hurley triangle setup uses two upper lights (one left, one right of
    # centre) plus a lower reflector or third light.  Each source leaves its own
    # distinct catchlight reflection, producing a recognisable 3-point geometry:
    # upper-left zone (~10-11 o'clock), upper-right zone (~1-2 o'clock), and
    # lower zone (~5-7 o'clock).
    #
    # No single-key setup produces all three zones simultaneously:
    #  - butterfly/clamshell: upper centre + lower only — no bilateral upper split
    #  - loop/rembrandt:      one upper lateral + no symmetrical upper opposite
    #  - ring_light:          donut shape, handled above
    #
    # Guard: must see all three zones across the combined bilateral catchlight
    # data.  A single-eye check is insufficient — one eye may be partially
    # occluded or angled away.  We check the union of both eyes' clock positions.
    if result.vision_data:
        _vd_cl_tri = result.vision_data.get("catchlights", {})
        _raw_cls_tri = (
            _vd_cl_tri.get("catchlights") or []
            if isinstance(_vd_cl_tri, dict) else []
        )
        _UPPER_LEFT  = {10, 11}
        _UPPER_RIGHT = {1, 2}
        _LOWER_TRI   = {5, 6, 7}
        _left_hrs, _right_hrs = [], []
        for _c in _raw_cls_tri:
            if not isinstance(_c, dict):
                continue
            try:
                _h = int(str(_c.get("position", "")).split()[0])
            except (ValueError, IndexError):
                continue
            if _c.get("eye") == "left":
                _left_hrs.append(_h)
            elif _c.get("eye") == "right":
                _right_hrs.append(_h)
        _all_hrs = _left_hrs + _right_hrs
        _has_ul = any(h in _UPPER_LEFT  for h in _all_hrs)
        _has_ur = any(h in _UPPER_RIGHT for h in _all_hrs)
        _has_lo = any(h in _LOWER_TRI   for h in _all_hrs)
        # Both eyes must individually show upper+lower (bilateral confirmation)
        _left_upper  = any(h in (_UPPER_LEFT | _UPPER_RIGHT | {12}) for h in _left_hrs)
        _left_lower  = any(h in _LOWER_TRI for h in _left_hrs)
        _right_upper = any(h in (_UPPER_LEFT | _UPPER_RIGHT | {12}) for h in _right_hrs)
        _right_lower = any(h in _LOWER_TRI for h in _right_hrs)
        _bilateral_ok = (_left_upper and _left_lower and _right_upper and _right_lower)

        # Handedness check: in a real Hurley triangle the upper-left key (10-11)
        # reflects PRIMARILY in the left eye, the upper-right key (1-2) PRIMARILY
        # in the right eye — they come from two physically separate sources.
        # A single large loop/butterfly source spanning both zones shows UL
        # catchlights equally in BOTH eyes (same physical source angle).
        # Require strict dominance: more UL hits in left than right, and more
        # UR hits in right than left.
        _left_ul_n  = sum(1 for h in _left_hrs  if h in _UPPER_LEFT)
        _right_ul_n = sum(1 for h in _right_hrs if h in _UPPER_LEFT)
        _left_ur_n  = sum(1 for h in _left_hrs  if h in _UPPER_RIGHT)
        _right_ur_n = sum(1 for h in _right_hrs if h in _UPPER_RIGHT)
        _handedness_ok = (_left_ul_n > _right_ul_n) and (_right_ur_n > _left_ur_n)

        if _has_ul and _has_ur and _has_lo and _bilateral_ok and _handedness_ok:
            logger.info(
                "[stage1] triangle — bilateral Hurley triangle catchlights: "
                "UL=%s UR=%s LO=%s left=%s right=%s",
                _has_ul, _has_ur, _has_lo, _left_hrs, _right_hrs,
            )
            return "triangle"

    # ── Silhouette key: hard backlight + no catchlights + full face shadow ──
    # Subject positioned between camera and bright background.  Signature:
    # bright background + zero catchlights + shadow_density ≈ 1.0.
    # Cannot be loop or rim: both have front-face detail and catchlights.
    #
    # ANALYSIS-ORDER ENFORCEMENT: do NOT gate on mode.get("no_face") here.
    # For silhouette subjects, face detection FAILS precisely because the face
    # is fully backlit — no_face=True is the expected state when this fires.
    # Gating on no_face would suppress the only pattern valid for backlit
    # subjects and collapse them into "unknown".  The three CV signals below
    # (bg_bright + no_catchlights + full_shadow) are strong enough alone.
    if result.vision_data and cr:
        _vd_bg = result.vision_data.get("background", {})
        _bg_bright = (
            isinstance(_vd_bg, dict) and
            _vd_bg.get("brightness_category") in ("bright", "very_bright", "white")
        )
        _ra = getattr(cr, "reflection_architecture", None)
        _no_catchlights = (
            _ra is None or getattr(_ra, "total_catchlights", 1) == 0
        )
        _ls = getattr(cr, "light_structure", None)
        _full_shadow = (
            _ls is not None and getattr(_ls, "shadow_density", 0) > 0.90
        )
        if _bg_bright and _no_catchlights and _full_shadow:
            logger.info("[stage1] silhouette_key — backlit + no catchlights + full shadow")
            return "silhouette_key"

        # ── Rim light: edge/separation light + deep shadow + dark background ──
        # Distinct from silhouette: silhouette has bright background; rim has dark.
        # The face is almost entirely in shadow but bright edge highlights are
        # visible on hair/contour.  The separation_light pass already detects
        # has_rim_light — we promote it to a definitive pattern when shadow
        # density confirms the face is unlit from the front.
        _sep = getattr(cr, "separation_light", None)
        _has_rim = (
            (isinstance(_sep, dict) and _sep.get("has_rim_light", False))
            or getattr(_sep, "has_rim_light", False)
        )
        if _has_rim and _full_shadow and not _bg_bright:
            logger.info("[stage1] rim — rim light + full shadow + dark background")
            return "rim"

    return None


# ═══════════════════════════════════════════════════════════════════════════
# 1. Full image analysis
# ═══════════════════════════════════════════════════════════════════════════

def analyze_image(
    image_path: str,
    *,
    run_extended: bool = True,
    run_vlm: bool = False,
    run_solver: bool = True,
    debug: bool = False,
    analysis_id_override: str | None = None,
) -> AnalysisResult:
    """Full analysis pipeline — single entry point for all image analysis.

    Implements the Expert Deconstruction Order (s61) — 7-layer stack:

      Layer 0 — Mode pre-read (no-face guard, B&W flag, HCG flag, scene type)
      Layer 1 — Raw CV signals (describe_image + 30-pass extended pipeline)
      Stage 1 — Definitive signature check (short-circuit for ring_light,
                 silhouette_key)
      Layer 2 — Primary physical interpretation (lighting_inference +
                 cue_inference: key_direction, key_elevation, shadow_pattern,
                 light_count)
      Layer 3 — Heuristic reconciliation (multi-light upgrades, soft-key
                 correction, B&W / pose clamshell guards)
      Layer 4 — Context / specialty layer (pose-relative broad/short,
                 source context annotation, specialty pattern upgrades)
      Layer 5 — Semantic hint layer (VLM — enrichment only, never a pattern candidate)
      Layer 6 — Resolver (reference_read > lighting_inference > cue_inference
                 > light_structure, priority-weighted; VLM is not a candidate)
      Layer 7 — Blueprint synthesis (setup_family, recreation_setup)

    Rule: geometry stays ahead of setup/source/style.  Face-shadow geometry
    (nose shadow position, direction, shape) anchors the pattern name.
    Source context, modifier identification, and setup-family naming are
    downstream refinements — they never replace geometry.

    Parameters
    ----------
    image_path : str
        Path to the image file.
    run_extended : bool
        Run the full 30+ pass extended pipeline. Default True.
    run_vlm : bool
        Include VLM analysis if available. Default False.
    run_solver : bool
        Run the solver chain (consensus, consistency, contradiction).
        Default True.
    debug : bool
        Preserve debug data (masks, img_bgr) for overlay generation.

    Returns
    -------
    AnalysisResult
        Structured result with all pipeline outputs.
    """
    import time as _time
    try:
        import sentry_sdk as _sentry
    except Exception:
        _sentry = None  # Sentry optional

    result = AnalysisResult()
    result.analysis_id = analysis_id_override or str(uuid.uuid4())
    _t0 = _time.perf_counter()

    # Start a Sentry transaction for the full analysis pipeline
    _txn = None
    if _sentry:
        try:
            _txn = _sentry.start_transaction(op="analysis", name="analyze_image")
            _sentry.get_current_scope().set_span(_txn)
            _txn.set_tag("analysis_id", result.analysis_id)
        except Exception:
            _txn = None

    def _span(op, desc):
        """Context manager for a Sentry child span (no-op if unavailable)."""
        if _txn:
            try:
                return _txn.start_child(op=op, description=desc)
            except Exception:
                pass
        from contextlib import nullcontext
        return nullcontext()

    # ── Step 1: Image description (cue extraction) ──────────────────
    raw = None  # initialise so the reference_read block can safely check `raw is not None`
    try:
        from engine.image_analysis import describe_image as _describe

        _t_step = _time.perf_counter()
        with _span("pipeline.describe", "Step 1: describe_image"):
            raw = _describe(image_path, "vision", debug=debug)
        _elapsed = round(_time.perf_counter() - _t_step, 2)
        result.stage_timings["describe_image"] = _elapsed
        logger.info("[analyze_image] Step 1 describe_image: %.1fs", _elapsed)
        result.description = {k: v for k, v in raw.items() if not k.startswith("_")}
        result.cue_report = raw.get("_cue_report")
        result.vlm_description = raw.get("_vlm_description")
        result.vlm_error = raw.get("_vlm_error")
        result.vision_data = raw.get("vision", {})
        result.classification = raw.get("classification", {})

        # Always populate debug_data when running extended pipeline — it
        # provides img_bgr, masks, and face_box that the vision passes need.
        # When debug=False but run_extended=True, we still need these for the
        # pipeline to function; they just won't be retained in the final result.
        if debug or run_extended:
            result.debug_data = {
                "img_bgr": raw.get("_debug_img_bgr"),
                "masks": raw.get("_debug_masks", {}),
                "face_box": raw.get("_debug_face_box"),
            }

        if not raw.get("ok"):
            result.ok = False
            result.notes.append("describe_image failed")
            return result
    except Exception as exc:
        logger.error("describe_image failed: %s", exc)
        result.ok = False
        result.notes.append(f"describe_image error: {exc}")
        return result

    # ── Layer 0: Mode pre-read ───────────────────────────────────────
    # Must run before extended pipeline so downstream B&W, HCG, and
    # no-face guards receive authoritative flags from Step 1 data.
    _layer0_mode_preread(result)

    # ── Layer 1 / Step 2: Extended pipeline (signal + synthesis passes) ─
    if run_extended:
        _t_step = _time.perf_counter()
        with _span("pipeline.extended", "Layer 1: extended_pipeline"):
            result.pipeline_results = _run_extended_pipeline(result)
        _elapsed = round(_time.perf_counter() - _t_step, 2)
        result.stage_timings["extended_pipeline"] = _elapsed
        logger.info("[analyze_image] Step 2 extended_pipeline: %.1fs", _elapsed)

        # Merge light_structure from extended pipeline into cue_report.
        # The cue_report was built in Step 1 (describe_image) before the
        # extended pipeline ran.  light_structure_pass requires face_box
        # and shadow_data that only become available in the extended pipeline.
        if result.pipeline_results and result.cue_report:
            _ls_data = result.pipeline_results.get("light_structure")
            if isinstance(_ls_data, dict) and _ls_data.get("ok") is not False:
                try:
                    from engine.cue_extraction import extract_light_structure
                    result.cue_report.light_structure = extract_light_structure(_ls_data)
                except Exception:
                    pass  # best-effort; cue_inference will use other signals

            # Merge catchlight shape from catchlight_pass (contour-based) into
            # the cue_report.  The vision-based circularity analysis overrides
            # the text-hint shape when it detects round catchlights.
            _cl_data = result.pipeline_results.get("catchlight")
            if isinstance(_cl_data, dict) and _cl_data.get("ok"):
                _cl_shape = _cl_data.get("catchlight_shape")
                _cl_circ = _cl_data.get("catchlight_circularity")
                if _cl_shape and _cl_shape != "unknown" and result.cue_report.catchlight_shape:
                    result.cue_report.catchlight_shape.dominant_shape = _cl_shape
                    if _cl_shape not in (result.cue_report.catchlight_shape.shapes_seen or []):
                        result.cue_report.catchlight_shape.shapes_seen.append(_cl_shape)
                # Store circularity for downstream use
                if _cl_circ is not None and result.cue_report.catchlight_shape is not None:
                    result.cue_report.catchlight_shape.notes = [
                        f"circularity={_cl_circ}"
                    ]

            # ── VLM catchlight shape enrichment ──────────────────────
            # CV circularity collapses all non-round shapes to "rectangular".
            # VLM can distinguish octagonal (beauty dish) and strip, which
            # are modifier-identifying shapes CV cannot resolve at small
            # catchlight scales.  Only applies when CV shape is low-precision
            # (unknown or rectangular) — never overwrites a CV round detection.
            if result.vlm_description is not None \
                    and result.cue_report \
                    and result.cue_report.catchlight_shape is not None:
                _vlm_sigs_cl = getattr(result.vlm_description, "signals", None)
                _vlm_cl_sig = getattr(_vlm_sigs_cl, "catchlights", None) \
                    if _vlm_sigs_cl else None
                _vlm_cl_shape = (
                    getattr(_vlm_cl_sig, "catchlight_shape", "") or ""
                ).lower() if _vlm_cl_sig else ""
                _cv_dom_shape = (
                    result.cue_report.catchlight_shape.dominant_shape or ""
                ).lower()
                if _vlm_cl_shape in ("octagonal", "strip") \
                        and _cv_dom_shape in ("", "unknown", "rectangular"):
                    result.cue_report.catchlight_shape.dominant_shape = _vlm_cl_shape
                    _seen = result.cue_report.catchlight_shape.shapes_seen or []
                    if _vlm_cl_shape not in _seen:
                        _seen.append(_vlm_cl_shape)
                        result.cue_report.catchlight_shape.shapes_seen = _seen
                    logger.debug(
                        "[vlm_cl_shape] VLM refined catchlight shape %r → %r",
                        _cv_dom_shape or "unknown", _vlm_cl_shape,
                    )

        # ── Full cue-report enrichment from extended pipeline ───────
        # enrich_cue_report_from_pipeline wires topology, highlight axis,
        # highlight symmetry, continuous source, bounce, separation,
        # off-axis key, light structure, and shadow penumbra into the
        # cue_report so all signals are available for cue inference.
        if result.pipeline_results and result.cue_report:
            try:
                # P6: Propagate face_yaw from vision_data.catchlights into
                # pipeline_results["catchlights"] so enrich_cue_report_from_pipeline
                # can build a FaceOrientation cue from it.
                _vy_cl = (result.vision_data or {}).get("catchlights", {})
                _face_yaw_val = _vy_cl.get("face_yaw") if isinstance(_vy_cl, dict) else None
                if _face_yaw_val is not None:
                    if not isinstance(result.pipeline_results.get("catchlights"), dict):
                        result.pipeline_results["catchlights"] = {}
                    result.pipeline_results["catchlights"]["face_yaw"] = _face_yaw_val

                from engine.cue_extraction import enrich_cue_report_from_pipeline
                result.cue_report = enrich_cue_report_from_pipeline(
                    result.cue_report, result.pipeline_results
                )
            except Exception as _enrich_exc:
                logger.warning("enrich_cue_report_from_pipeline failed: %s", _enrich_exc)

        # ── CV warm/cool split detection ──────────────────────────────────────
        # Compare highlight CCT (key region, P85 luminance) against shadow CCT
        # (shadow region, P30).  A gap >1500K with key warm (<4500K) and shadow
        # cool (>5500K) is a deliberate creative split — not noise.  We set
        # cue_report.warm_cool_split so reference_read can annotate gel notes
        # without relying on VLM.
        if result.pipeline_results and result.cue_report:
            _ct_data = result.pipeline_results.get("color_temp") or {}
            if isinstance(_ct_data, dict) and _ct_data.get("ok"):
                _cct_sources = _ct_data.get("cct_sources") or []
                _key_cct: Optional[float] = None
                _shad_cct: Optional[float] = None
                for _src in _cct_sources:
                    if not isinstance(_src, dict):
                        continue
                    _region = (_src.get("region") or "").lower()
                    _cct_val = _src.get("cct_kelvin")
                    if _cct_val is None:
                        continue
                    if "highlight" in _region or "key" in _region:
                        _key_cct = float(_cct_val)
                    elif "shadow" in _region or "fill" in _region:
                        _shad_cct = float(_cct_val)
                if (
                    _key_cct is not None and _shad_cct is not None
                    and _key_cct < 4500
                    and _shad_cct > 5500
                    and (_shad_cct - _key_cct) > 1500
                ):
                    result.cue_report.warm_cool_split = True
                    logger.info(
                        "[orchestrator] CV warm/cool split: key=%.0fK shadow=%.0fK gap=%.0fK",
                        _key_cct, _shad_cct, _shad_cct - _key_cct,
                    )

        # When debug=False, free heavy image data now that the pipeline is done.
        if not debug:
            result.debug_data.pop("img_bgr", None)
            result.debug_data.pop("masks", None)

    # ── Stage 1: Definitive signature check (short-circuit) ──────────
    # Runs after Layer 1 CV signals are available but BEFORE Layer 2
    # geometry trees.  If a pattern is identified BEYOND DOUBT from a
    # single feature (ring donut catchlights, full-shadow silhouette), set
    # result.definitive_pattern so the resolver uses it at priority 0.
    # Layers 2-5 still run for enrichment (reconstruction, setup_family)
    # but cannot override a definitive signature.
    _t_step = _time.perf_counter()
    _def = _stage1_definitive_signatures(result)
    if _def:
        result.definitive_pattern = _def
        result.notes.append(f"stage1_definitive: {_def}")
    result.stage_timings["stage1_signatures"] = round(_time.perf_counter() - _t_step, 4)

    # ── Layer 2 / Step 3: Lighting inference (catchlight topology) ────
    # Primary: key_direction, key_elevation, shadow_pattern, light_count.
    # Uses upper catchlights only for key elevation (lower catchlights are
    # fill evidence — spec §4, Stage 4).
    try:
        from engine.lighting_inference import infer_lighting_from_vision

        _t_step = _time.perf_counter()
        with _span("pipeline.lighting", "Layer 2: lighting_inference"):
            result.lighting_intel = infer_lighting_from_vision(
                result.vision_data,
                classification=result.classification,
                cue_report=result.cue_report,
                vlm_description=result.vlm_description,
            )
        _elapsed = round(_time.perf_counter() - _t_step, 2)
        result.stage_timings["lighting_inference"] = _elapsed
        logger.info("[analyze_image] Layer 2 lighting_inference: %.1fs", _elapsed)
    except Exception as exc:
        logger.warning("Lighting inference failed: %s", exc)
        result.notes.append(f"lighting_inference skipped: {exc}")

    # ── Definitive-pattern light_count override ───────────────────────
    # When Stage 1 fired a definitive signature, the authoritative light count
    # is known from the physical setup, not from catchlight deduplication.
    # Apply now so all downstream stages see the correct count.
    _DEFINITIVE_LIGHT_COUNTS = {
        "ring_light":     1,  # single annular source
        "silhouette_key": 1,  # single backlight
        "rim":            1,  # single edge/separation source
        "triangle":       3,  # two upper keys + one lower fill/reflector
    }
    if result.definitive_pattern and result.definitive_pattern in _DEFINITIVE_LIGHT_COUNTS:
        _def_lc = _DEFINITIVE_LIGHT_COUNTS[result.definitive_pattern]
        if result.lighting_intel is not None:
            result.lighting_intel.light_count = _def_lc
        logger.info(
            "[stage1] light_count forced to %d for definitive pattern '%s'",
            _def_lc, result.definitive_pattern,
        )

    # ── Layer 2 / Step 4: Cue inference (shadow geometry) ────────────
    # Face-shadow geometry: nose shadow position, direction, shape →
    # authoritative shadow_pattern.  This is what photographers name first.
    # VLM hints do NOT influence this stage (spec §5: semantic hints enter
    # after Layer 2 is grounded).
    cue_inference_result = None
    if result.cue_report:
        try:
            from engine.cue_inference import run_cue_inference_pipeline

            _t_step = _time.perf_counter()
            with _span("pipeline.cue", "Layer 2: cue_inference"):
                cue_inference_result = run_cue_inference_pipeline(result.cue_report)
            _elapsed = round(_time.perf_counter() - _t_step, 2)
            result.stage_timings["cue_inference"] = _elapsed
            logger.info("[analyze_image] Step 4 cue_inference: %.1fs", _elapsed)
            result.cue_inference_result = cue_inference_result
        except Exception as exc:
            logger.warning("Cue inference failed: %s", exc)
            result.notes.append(f"cue_inference skipped: {exc}")

    # ── Layer 3 support / Step 5: Solver chain ──────────────────────
    # Consensus → consistency → contradiction.  Feeds contradiction flags
    # into the Layer 6 resolver's demotion logic.
    if run_solver and result.pipeline_results:
        _t_step = _time.perf_counter()
        with _span("pipeline.solver", "Layer 3: solver_chain"):
            result.solver_result = _run_solver_chain(
                result.pipeline_results,
                result.cue_report,
                cue_inference_result,
                result.vision_data,
            )
        _elapsed = round(_time.perf_counter() - _t_step, 2)
        result.stage_timings["solver_chain"] = _elapsed
        logger.info("[analyze_image] Layer 3 solver_chain: %.1fs", _elapsed)

    # ── Layer 6 input / Step 6: Reference read ───────────────────────
    # Three-layer analysis (shadow/highlight/gobo) — the richest single
    # analysis.  Becomes the highest-priority candidate in the resolver
    # unless signal contradictions demote it (see resolve_pattern_candidates).
    try:
        from engine.reference_read import build_reference_photo_analysis

        _t_step = _time.perf_counter()
        with _span("pipeline.reference_read", "Layer 6: reference_read"):
            result.reference_analysis = build_reference_photo_analysis(
                vision_data=result.vision_data,
                classification=result.classification,
                cue_report=result.cue_report,
                lighting_intel=result.lighting_intel,
                image_analysis=raw if raw is not None else result.description,
                vlm_description=result.vlm_description,
            )
        _elapsed = round(_time.perf_counter() - _t_step, 2)
        result.stage_timings["reference_read"] = _elapsed
        logger.info("[analyze_image] Layer 6 reference_read: %.1fs", _elapsed)
    except Exception as exc:
        logger.warning("Reference read failed: %s", exc)
        result.notes.append(f"reference_read skipped: {exc}")

    # ── Extract VLM reconstruction if available ─────────────────────
    if result.pipeline_results:
        result.vlm_reconstruction = result.pipeline_results.get("vlm_reconstruction")

    # ── Layer 5: VLM semantic hint ───────────────────────────────────
    # Built AFTER Layer 2 geometry is settled.  VLM enriches the result
    # (environment, source_quality hints, reconstruction) but is NOT a
    # pattern candidate — it never participates in pattern election.
    result.vlm_semantic_hint = build_vlm_semantic_hint(result.vlm_description)

    # ── Layer 6: Resolver ────────────────────────────────────────────
    # Priority: definitive_signature(-1) > reference_read(0) >
    # lighting_inference(1) > cue_inference(2) > light_structure(3).
    # VLM is not a candidate.  Contradiction demotion can shift
    # reference_read from 0→2 when CV signals strongly contradict it.
    with _span("pipeline.resolver", "Layer 6: resolve_pattern_candidates"):
        pc = resolve_pattern_candidates(result)
    result.pattern_candidates = pc
    result.authoritative_pattern = pc.authoritative_pattern
    result.authoritative_pattern_source = pc.authoritative_source
    result.pattern_confidence = pc.primary.confidence
    result.pattern_confidence_label = pc.confidence_label

    # ── Layer 3: Heuristic reconciliation ────────────────────────────
    # Multi-light upgrades run AFTER the resolver so the base pattern is
    # settled.  These are not primary classifiers — they are topology
    # confirmations that promote an already-resolved base pattern.
    # Butterfly → Clamshell upgrade ─────────────────────────────
    # Clamshell IS butterfly + fill from below.  If the pattern resolved
    # to butterfly but we detect 2+ lights, upgrade to clamshell.
    # This catches cases where cue_inference's clamshell guard missed
    # due to light_count disagreement between classifiers.
    #
    # Signal guard: a real clamshell creates visible secondary shadows from
    # two distinct sources.  If shadow_density ≈ 0 and asymmetry ≈ 0,
    # the "extra lights" are likely specular reflections (e.g. from glasses,
    # jewelry), not actual fill sources.  Keep butterfly in that case.
    _cr = getattr(result, "cue_report", None)
    _ls = getattr(_cr, "light_structure", None) if _cr else None
    _clam_shadow_ok = True
    if _ls is not None:
        _clam_sd = getattr(_ls, "shadow_density", 0.1)
        _clam_lr = getattr(_ls, "left_right_asymmetry", 0.1)
    else:
        _clam_sd = 0.1
        _clam_lr = 0.1
    # lighting_intel override: when lighting_intel explicitly classifies the
    # scene as "clamshell" (catchlight topology shows above+below sources),
    # trust that signal and allow the upgrade even when shadow measurements
    # are low (a strong fill-from-below reflector naturally reduces shadow_density).
    # Measured: beauty clamshell — li.pattern="clamshell", shadow_density ~0.08.
    _li_says_clamshell = (
        getattr(result.lighting_intel, "pattern", "") == "clamshell"
        if result.lighting_intel else False
    )
    if _ls is not None and not _li_says_clamshell:
        # Tightened from (0.02, 0.02): genuine clamshell produces visible
        # secondary shadows from the fill-below source.  When shadow density
        # and asymmetry are both low AND lighting_intel doesn't confirm
        # clamshell, the "extra lights" are likely hair/background lights.
        if _clam_sd < 0.12 and _clam_lr < 0.05:
            _clam_shadow_ok = False  # butterfly-level signals — not clamshell
        # High LR asymmetry guard: clamshell is inherently symmetric (key
        # above + fill below, both on-axis).  Strong left-right asymmetry
        # (> 0.30) means the lighting is directional — incompatible with
        # clamshell.  The extra catchlight is from a reflector or ambient
        # bounce on one side, not a dedicated below-key fill.
        # Measured: butterfly lr=0.433 → directional, not clamshell.
        #   clamshell_clean lr≈0.05 → symmetric, correct clamshell.
        if _clam_lr > 0.30:
            _clam_shadow_ok = False  # directional → not on-axis clamshell
    # Extreme-bilateral guard: when hs.symmetry is near-perfect (> 0.95)
    # AND fill is flat (> 0.90) AND shadow_density is significant (> 0.40),
    # the combination is paradoxical — a single strong directional key
    # creating bilateral-looking illumination through tonal processing.
    # NOT fill-from-below clamshell (clamshell has LOW shadow_density).
    # Measured: dramatic-makeup butterfly hs.sym=0.99, fill_ratio=1.0,
    # shadow_density=0.57.  All three must be true to block the upgrade.
    _hs_clam = getattr(_cr, "highlight_symmetry", None) if _cr else None
    _hs_sym_clam = getattr(_hs_clam, "symmetry_score", 0.0) if _hs_clam else 0.0
    _fr_clam = getattr(_cr, "fill_ratio", None) if _cr else None
    _fr_ratio_clam = getattr(_fr_clam, "ratio", 0.5) if _fr_clam else 0.5
    _is_paradox_bilateral = (
        _hs_sym_clam > 0.95       # near-perfect symmetry
        and _fr_ratio_clam > 0.90  # flat fill
        and _clam_sd > 0.40        # significant shadow — contradicts fill-from-below
    )
    if _is_paradox_bilateral:
        _clam_shadow_ok = False  # paradoxical single-key bilateral → keep butterfly

    def _upgrade_pattern(new_pattern: str) -> None:
        """Replace pc.primary in-place and purge stale same-pattern alternates.

        Used by all heuristic upgrade blocks so the mutation is consistent:
        - pc.primary gets a fresh PatternCandidate with the upgraded pattern
          but retains the original source and confidence.
        - pc.alternates has any un-demoted entries that matched the OLD primary
          pattern removed — they were lower-priority duplicates of the original
          decision and are now stale after the upgrade.
        - result.authoritative_pattern is updated to match.
        Note: result.pattern_candidates still points to `pc` (same object),
        so no reassignment of result.pattern_candidates is needed.
        """
        old_pattern = pc.primary.pattern
        pc.primary = PatternCandidate(
            pattern=new_pattern,
            source=pc.primary.source,
            confidence=pc.primary.confidence,
            rank=1,
        )
        # Remove alternates that duplicated the old primary pattern without
        # being demoted — they were lower-priority agreement with the old call
        # and are now stale.
        _demoted_sfx = ("_demoted",)
        pc.alternates = [
            a for a in pc.alternates
            if not (a.pattern == old_pattern and not a.source.endswith(_demoted_sfx))
        ]
        result.authoritative_pattern = new_pattern

    if pc.authoritative_pattern == "butterfly":
        li_count = getattr(result.lighting_intel, "light_count", 0) if result.lighting_intel else 0
        # ── Fill-ratio clamshell detection ──────────────────────────────────
        # A clamshell setup (key above + fill below) produces:
        #   - fill_ratio > 0.85  (fill nearly matches key brightness)
        #   - shadow_density ≈ 0  (fill cancels key shadow)
        #   - butterfly geometry  (on-axis key)
        # The light_count may read 1 because both sources sit on-axis at 12
        # o'clock — CV can't separate two coaxial lights.  fill_ratio > 0.85
        # is direct evidence of a second (fill) light source, which is the
        # physical definition of clamshell vs butterfly.
        # Guard: only apply when butterfly is the base AND catchlights are
        # at/near 12 o'clock (confirming on-axis key placement — off-axis
        # key + high fill is loop-with-reflector, not clamshell).
        _pk_ci = getattr(result.lighting_intel, "catchlight_intelligence", None) or {}
        _pk_ci = _pk_ci if isinstance(_pk_ci, dict) else {}
        _pk_pos_str = (_pk_ci.get("primary_key") or {}).get("position", "")
        try:
            _pk_h_clam = int(_pk_pos_str.split()[0])
        except (ValueError, IndexError):
            _pk_h_clam = None
        _pk_near_12 = _pk_h_clam in (11, 12, 1)  # on-axis or near-on-axis

        # Tight shadow gate: sd < 0.03 separates true clamshell (fill from
        # below almost completely cancels key shadow → sd ≈ 0.01) from
        # butterfly + passive reflector (sd ≈ 0.05-0.10 — reflector bounce
        # doesn't cancel as effectively as an active fill light).
        _fill_ratio_clamshell = (
            _fr_ratio_clam > 0.85
            and _pk_near_12
            and _clam_sd < 0.03
        )

        # Catchlight position gate (analysis-order rule: catchlights before
        # pattern).  Clamshell requires an ON-AXIS key (catchlight at 11/12/1).
        # An off-axis catchlight (e.g. 10 o'clock) means the key is offset —
        # that's loop-with-fill, not clamshell, regardless of fill_ratio or
        # light_count.  When no catchlight is detected, allow the upgrade
        # (can't confirm or deny on-axis).
        _pk_off_axis_clam = _pk_h_clam is not None and _pk_h_clam not in (11, 12, 1)
        if not _pk_off_axis_clam:
            if (li_count >= 2 and _clam_shadow_ok) or _fill_ratio_clamshell:
                _upgrade_pattern("clamshell")

    # ── Rembrandt contradiction guard ────────────────────────────────
    # Demote rembrandt → loop when CV evidence contradicts it.
    # Rembrandt is defined by a triangle of light on the shadow-side
    # cheek.  If light_structure explicitly finds NO triangle AND
    # highlight_symmetry is high (> 0.65 — both sides of the face
    # are similarly lit), the "rembrandt" label from cue_inference /
    # VLM is not physically grounded.  Fall back to "loop" (the
    # simpler, less-specific single-key pattern).
    # This is a GUARDRAIL (VL): VLM hints must not outrank strong
    # physical (CV) evidence of absence.
    if pc.authoritative_pattern == "rembrandt":
        _ls_rg = getattr(_cr, "light_structure", None) if _cr else None
        _hs_rg = getattr(_cr, "highlight_symmetry", None) if _cr else None
        _tri_det = getattr(_ls_rg, "triangle_detected", None) if _ls_rg else None
        _tri_comp = getattr(_ls_rg, "triangle_completeness", 0.5) if _ls_rg else 0.5
        _hs_sym_rg = getattr(_hs_rg, "symmetry_score", 0.5) if _hs_rg else 0.5
        _sd_rg = getattr(_ls_rg, "shadow_density", 0.1) if _ls_rg else 0.1
        if (
            _tri_det is False                  # CV explicitly says no triangle
            and _tri_comp < 0.15               # triangle completeness near-zero
            and _hs_sym_rg > 0.65              # both sides of face similarly lit
            and _sd_rg < 0.02                  # almost no shadow on face
        ):
            _upgrade_pattern("loop")
            pc.primary.supporting_cues.append("rembrandt_cv_contradiction_demoted")

    # ── Overfill flat override ────────────────────────────────────────
    # When the winning pattern is "loop" but THREE independent physical
    # signals all report extreme bilateral evenness, the "loop" is from
    # a noise-level centroid on a face with essentially zero shadow.
    # Override to "flat".  Thresholds are deliberately extreme so this
    # only fires for genuine overfill/multi-source-cancel scenarios,
    # not for fill-heavy loops which still have shadow_density ~0.04+.
    if pc.authoritative_pattern == "loop":
        _hs_of = getattr(_cr, "highlight_symmetry", None) if _cr else None
        _hs_sym_of = getattr(_hs_of, "symmetry_score", 0.0) if _hs_of else 0.0
        _ls_of = getattr(_cr, "light_structure", None) if _cr else None
        _sd_of = getattr(_ls_of, "shadow_density", 1.0) if _ls_of else 1.0
        _fr_of = getattr(_cr, "fill_ratio", None) if _cr else None
        _fr_ratio_of = getattr(_fr_of, "ratio", 0.0) if _fr_of else 0.0
        if _sd_of < 0.03 and _hs_sym_of > 0.99 and _fr_ratio_of > 0.95:
            _upgrade_pattern("flat")
            pc.primary.supporting_cues.append("overfill_flat_override")

    # ── Shadow-continuity Rembrandt rescue ───────────────────────────
    # When the winning pattern is "loop" but shadow_continuity independently
    # found a connected cheek triangle (the Rembrandt triangle), upgrade
    # to rembrandt.  This catches cases where all classifiers mislabel a
    # filled Rembrandt as loop (centroid nearly vertical, lr_asym compressed
    # by fill) but the connected-component shadow analysis correctly
    # identifies the characteristic bright cheek patch within the shadow.
    #
    # Guard: require shadow_continuity.triangle_connected=True AND
    # connectivity_score > 0.70 AND confidence > 0.40 — same thresholds
    # as the loop contradiction path in _signal_contradiction_score.
    # Additional guard: hs_sym < 0.50 (one side in shadow — bilateral
    # highlights would mean the "triangle" is from makeup/bone, not light).
    if pc.authoritative_pattern == "loop":
        _sc_rescue = getattr(_cr, "shadow_continuity", None)
        _hs_rescue = getattr(_cr, "highlight_symmetry", None)
        _hs_sym_rescue = getattr(_hs_rescue, "symmetry_score", 0.5) if _hs_rescue else 0.5
        if (
            _sc_rescue is not None
            and getattr(_sc_rescue, "triangle_connected", False)
            and getattr(_sc_rescue, "connectivity_score", 0.0) > 0.70
            and getattr(_sc_rescue, "confidence", 0.0) > 0.40
            and _hs_sym_rescue < 0.50
        ):
            _upgrade_pattern("rembrandt")
            pc.primary.supporting_cues.append("shadow_continuity_triangle_rescue")

    # ── Short/loop → Rembrandt triangle rescue via tri_iso ─────────────
    # When the base pattern is "short" or "loop" but light_structure
    # triangle_isolation is definitive (> 0.80) with sufficient shadow
    # density (> 0.15), the Rembrandt triangle IS present — upgrade.
    # Short + visible triangle = Rembrandt (the triangle is the defining
    # feature regardless of which cheek the key illuminates).
    # Loop + strong triangle = Rembrandt (loop that went far enough for
    # the nose shadow to connect to cheek shadow).
    # Guard: hs_sym < 0.70 — bilateral highlights would mean the
    # "triangle" is ambient/makeup, not a shadow-enclosed light patch.
    # Measured: 0aec889 (dark skin) has base=short, tri=3.028, sd=0.699.
    if pc.authoritative_pattern in ("short", "loop", "split"):
        _ls_tri_r = getattr(_cr, "light_structure", None) if _cr else None
        _tri_iso_r = getattr(_ls_tri_r, "triangle_isolation", 0.0) if _ls_tri_r else 0.0
        _sd_r = getattr(_ls_tri_r, "shadow_density", 0.0) if _ls_tri_r else 0.0
        _hs_tri_r = getattr(_cr, "highlight_symmetry", None) if _cr else None
        _hs_sym_tri_r = getattr(_hs_tri_r, "symmetry_score", 0.5) if _hs_tri_r else 0.5
        if _tri_iso_r > 0.80 and _sd_r > 0.15 and _hs_sym_tri_r < 0.70:
            _upgrade_pattern("rembrandt")
            pc.primary.supporting_cues.append("tri_iso_rembrandt_rescue")

    # ── Hurley triangle upgrade: catchlight-first, VLM fallback ─────────
    # The Hurley triangle is a 3-point lighting setup (key + two fills/accents)
    # defined by its catchlight pattern: 3 distinct catchlights forming a
    # triangular arrangement in the eyes (analysis-order: catchlights before
    # pattern).
    #
    # Primary path: 3+ distinct catchlight sources from reflection_architecture
    # (deduped, per-eye).  This is the definitive signal — the triangle
    # arrangement IS the pattern.
    #
    # Fallback path: when catchlights are undetectable (small eyes, bright bg
    # washout, B&W), use VLM light_count + physical signals.  This is a
    # SEMANTIC HINT (guardrail VL) — weaker than catchlight evidence but
    # the only available signal when CV catchlight detection fails.
    # Requires: 3+ VLM lights + bright bg + high brightness + hs > 0.85.
    if pc.authoritative_pattern in ("loop", "butterfly"):
        _tri_ra = getattr(_cr, "reflection_architecture", None) if _cr else None
        _tri_cl_total = getattr(_tri_ra, "total_catchlights", 0) if _tri_ra else 0
        _tri_bg = getattr(_cr, "background_illumination", None) if _cr else None
        _tri_bg_bright = _tri_bg and getattr(_tri_bg, "brightness_relative", "") == "brighter"
        _tri_hs = getattr(_cr, "highlight_symmetry", None) if _cr else None
        _tri_hs_sym = getattr(_tri_hs, "symmetry_score", 0.0) if _tri_hs else 0.0
        _tri_cls = result.classification or {}
        _tri_bright = _tri_cls.get("brightness", "") in ("high", "very_high")
        _tri_raw_lc = getattr(result.lighting_intel, "light_count", 0) if result.lighting_intel else 0

        # Primary: catchlight-based triangle detection (analysis-order:
        # catchlights before pattern). Requires 3+ deduped catchlights
        # AND bright background AND high symmetry — raw count alone is
        # insufficient (specular noise from B&W, ring lights, etc.).
        _tri_catchlight_ok = _tri_cl_total >= 3 and _tri_bg_bright and _tri_hs_sym > 0.85

        # Fallback: VLM + physical signals when catchlights unavailable
        _tri_vlm_ok = (
            _tri_raw_lc >= 3 and _tri_bg_bright and _tri_bright
            and _tri_hs_sym > 0.85 and _tri_cl_total < 3  # only when catchlights fail
        )

        if _tri_catchlight_ok or _tri_vlm_ok:
            _upgrade_pattern("triangle")
            _src = "catchlight_triangle" if _tri_catchlight_ok else "vlm_triangle_fallback"
            pc.primary.supporting_cues.append(_src)

    # ── Layer 4: Corrective / context layer ─────────────────────────
    # Pose-relative correction (spec Stage 13): loop→broad, rembrandt→short.
    # Runs AFTER shadow_pattern is settled in Layer 2/3.  Blocked when
    # ── Pose resolver: broad/short disambiguation ─────────────────────
    # Broad and short are *face-pose-relative* patterns — the shadow
    # geometry alone is ambiguous; only face turn direction relative to
    # key position disambiguates:
    #   • broad:  key on the BROAD side (side facing camera)
    #   • short:  key on the SHORT side (narrow side, away from camera)
    #
    # Source of truth: cue_report.face_orientation (computed by
    # cue_extraction from MediaPipe face landmarks).
    #   - broad_side: image-direction ("left"/"right") where key = broad
    #   - short_side: image-direction ("left"/"right") where key = short
    #   - confidence: 0.0 (frontal) → 0.80 (significant turn)
    #
    # Guard conditions (all must pass):
    #   1. face_orientation available and confidence >= 0.65
    #      (moderate or significant turn — frontal is indeterminate)
    #   2. key_side is resolved left or right (not center/unknown)
    #   3. scene is studio-controlled OR yaw is significant (>= 0.80)
    #      (natural-light portraits with window-side key can resemble
    #      broad/short but the distinction matters less there)
    #
    # Patterns eligible for pose upgrade:
    #   butterfly, loop, rembrandt, split → broad or short
    #   low_key (geometric butterfly + dark BG) → short or broad
    #   (clamshell, high_key, triangle are definitively non-directional)
    _cl_info = result.vision_data.get("catchlights", {}) if result.vision_data else {}
    _face_yaw = _cl_info.get("face_yaw")
    _esc_bs = getattr(_cr, "environmental_shadow_continuity", None) if _cr else None
    _env_hints_bs = getattr(_esc_bs, "environment_hints", []) if _esc_bs else []
    _studio_bg = "controlled_background" in _env_hints_bs
    _fo = getattr(_cr, "face_orientation", None) if _cr else None
    _fo_conf = getattr(_fo, "confidence", 0.0) if _fo else 0.0
    _fo_broad = getattr(_fo, "broad_side", "unknown") if _fo else "unknown"
    _fo_short = getattr(_fo, "short_side", "unknown") if _fo else "unknown"
    _fo_yaw = getattr(_fo, "yaw", 0.0) if _fo else 0.0

    # Normalize key_side: "upper_left"→"left", "upper_right"→"right", etc.
    def _normalize_side(ks: str) -> str:
        if "left" in ks:
            return "left"
        if "right" in ks:
            return "right"
        return ks  # "center", "unknown"

    _ks_norm = _normalize_side(
        getattr(result.lighting_intel, "key_side", "unknown")
        if result.lighting_intel else "unknown"
    )
    _pose_scene_ok = _studio_bg or abs(_fo_yaw) >= 0.40
    _pose_eligible = {
        "butterfly", "loop", "rembrandt", "split", "low_key",
    }

    if (
        _fo_conf >= 0.65
        and _ks_norm in ("left", "right")
        and _fo_broad in ("left", "right")
        and _pose_scene_ok
        and pc.authoritative_pattern in _pose_eligible
    ):
        if _ks_norm == _fo_broad:
            _upgrade_pattern("broad")
            pc.primary.supporting_cues.append("pose_resolver:broad")
            logger.info(
                "[pose_resolver] %s → broad  (key_side=%s, broad_side=%s, yaw=%.3f)",
                pc.primary.pattern, _ks_norm, _fo_broad, _fo_yaw,
            )
        elif _ks_norm == _fo_short:
            # Guard: when tri_iso rescue confirmed a definitive Rembrandt
            # triangle, do NOT downgrade to short. The triangle IS the
            # defining feature — short-side Rembrandt is still Rembrandt.
            _tri_rescued_pr = "tri_iso_rembrandt_rescue" in getattr(pc.primary, "supporting_cues", [])
            if not _tri_rescued_pr:
                _upgrade_pattern("short")
                pc.primary.supporting_cues.append("pose_resolver:short")
                logger.info(
                    "[pose_resolver] %s → short  (key_side=%s, short_side=%s, yaw=%.3f)",
                    pc.primary.pattern, _ks_norm, _fo_short, _fo_yaw,
                )

    # Legacy yaw-magnitude rules (kept for split→broad edge cases not
    # caught by the pose resolver above due to key_side being unknown).
    if _face_yaw is not None and _studio_bg:
        _abs_yaw = abs(_face_yaw)
        if pc.authoritative_pattern == "split" and _abs_yaw > 0.2:
            _upgrade_pattern("broad")
        elif pc.authoritative_pattern == "rembrandt" and _abs_yaw > 0.5:
            # Guard: when tri_iso rescue confirmed a definitive Rembrandt
            # triangle (supporting cue present), do NOT downgrade to short.
            # The triangle IS the defining feature — calling it "short"
            # loses the most important diagnostic information.
            # Short-side Rembrandt is still Rembrandt.
            _tri_rescued = "tri_iso_rembrandt_rescue" in getattr(pc.primary, "supporting_cues", [])
            if not _tri_rescued:
                _upgrade_pattern("short")

    # ── Specialty pattern post-classification ───────────────────────
    # Check environmental/tonal signals to upgrade geometric patterns
    # to specialty names (high_key, low_key, window_portrait, etc.).
    # The geometric pattern is preserved in pattern_candidates; the
    # specialty name becomes the authoritative pattern.
    specialty = _apply_specialty_pattern(result)
    if specialty:
        result.authoritative_pattern = specialty
        result.authoritative_pattern_source = f"specialty:{result.authoritative_pattern_source}"

    # ── Sync pattern_confidence after all upgrades ───────────────────
    # Pattern upgrades (butterfly→clamshell, loop→triangle, specialty) may
    # change the authoritative pattern but the PatternCandidates.primary was
    # updated in-place.  Re-sync the scalar fields so callers get consistent
    # values without needing to reach into pattern_candidates.
    if result.pattern_candidates and result.pattern_candidates.primary:
        result.pattern_confidence = result.pattern_candidates.primary.confidence
        result.pattern_confidence_label = result.pattern_candidates.confidence_label

    # ── Phase 1: conservative pattern_status assignment ──────────────
    # Assigned here — after all upgrades — when the final authoritative
    # pattern is settled.  Rules (in priority order):
    #   FUSED_FINAL  — a real pattern was resolved from ranked candidates
    #   CONTESTED    — resolver found material contradictions between classifiers
    #   ASSUMED      — no signal; fell back to "unknown" explicitly
    #   UNKNOWN      — none of the above; pre-resolution default
    _src = result.authoritative_pattern_source or ""
    _pat = result.authoritative_pattern or ""
    if _pat and _pat not in ("unknown", ""):
        result.pattern_status = FieldStatus.FUSED_FINAL
    elif "contradiction" in _src or "contested" in _src:
        result.pattern_status = FieldStatus.CONTESTED
    elif _pat in ("unknown", "") and _src in ("none", "fallback", ""):
        result.pattern_status = FieldStatus.ASSUMED
    else:
        result.pattern_status = FieldStatus.UNKNOWN

    # ── Phase 1: attach version metadata snapshot ────────────────────
    # Populated once, after all resolution is complete.  Do not mutate.
    # system_version is a best-effort import; falls back to "unknown" if
    # ENGINE_VERSION is not defined in the importing service.
    try:
        from recommend_service import ENGINE_VERSION as _ev  # type: ignore[import]
    except Exception:
        _ev = "unknown"
    result.version_metadata = AnalysisVersionMetadata(
        system_version=_ev,
        taxonomy_version=TAXONOMY_VERSION,
        pipeline_version=PIPELINE_VERSION,
    )

    # ── Phase 2: pattern_provenance — structure-first ────────────────
    # FieldProvenance wraps the settled PatternCandidates into a single
    # auditable record for the pattern field.
    #
    # supporting_cues is intentionally [] in Phase 2.
    # Phase 3 will thread cue names from _signal_contradiction_score()
    # into PatternCandidate, then propagate them into this record.
    if result.pattern_candidates is not None:
        _pc = result.pattern_candidates
        _alts = [
            _FieldCandidate(
                value=a.pattern,
                source=a.source,
                confidence=a.confidence,
                supporting_cues=[],  # Phase 3: thread from resolver
                demotion_reason=("demoted" if "demoted" in (a.source or "") else ""),
                status=(
                    FieldStatus.INFERRED if a.pattern not in ("unknown", "") else
                    FieldStatus.ASSUMED
                ),
            )
            for a in _pc.alternates
        ]
        result.pattern_provenance = FieldProvenance(
            field_name="pattern",
            value=result.authoritative_pattern,
            status=result.pattern_status,
            confidence=result.pattern_confidence,
            source=result.authoritative_pattern_source,
            supporting_cues=list(_pc.primary.supporting_cues),
            alternates=_alts,
            demotion_applied="demoted" in (result.authoritative_pattern_source or ""),
            pipeline_stage="fusion",
        )

    # ── Phase 3: VLM disagreements record ──────────────────────────────
    # Additive alongside VLWDimensionResult — not a replacement.
    # Uses canonical field names for cross-pipeline auditing.
    if result.vlm_semantic_hint is not None and result.pattern_candidates is not None:
        result.vlm_disagreements = build_vlm_disagreements(
            vlm_hint=result.vlm_semantic_hint,
            pattern_candidates=result.pattern_candidates,
            resolved_pattern=result.authoritative_pattern,
            resolved_source=result.authoritative_pattern_source,
            pipeline_version=PIPELINE_VERSION,
        )
        # Phase 5a — persist best-effort; never break analysis on DB failure
        if result.vlm_disagreements and result.analysis_id:
            try:
                from db.database import save_vlm_disagreements
                save_vlm_disagreements(result.analysis_id, result.vlm_disagreements)
            except Exception:
                pass  # DB persistence is best-effort; analysis result is unaffected

    # ── Signal-based light_count correction ─────────────────────────
    # Butterfly/paramount is physically a single overhead source.  The
    # catchlight pipeline often over-counts because BW blown-highlight
    # reflections, specular makeup, and floor bounce all register as
    # additional catchlights.  When the pattern is authoritative butterfly,
    # force light_count=1 unless the shadow analysis shows clear evidence
    # of a dedicated second source (fill below, strong lateral asymmetry).
    # Thresholds: sd < 0.15 allows the normal nose-shadow cast by
    # butterfly (measured ~0.08); lr < 0.10 allows minor asymmetry from
    # slight head turn but blocks genuine lateral fill.
    if result.lighting_intel is not None and _ls is not None:
        _corr_sd = getattr(_ls, "shadow_density", 0.5)
        _corr_lr = getattr(_ls, "left_right_asymmetry", 0.5)
        _corr_pat = result.authoritative_pattern
        _corr_lc = getattr(result.lighting_intel, "light_count", 0)
        if _corr_pat == "butterfly" and _corr_sd < 0.15 and _corr_lr < 0.10 and _corr_lc > 1:
            result.lighting_intel.light_count = 1

    # ── Single-key patterns: passive fill is not a light source ────────
    # Loop, rembrandt, split, broad, and short are single-key setups
    # where the "fill" is typically a reflector, bounce card, or ambient
    # room light — NOT a second strobe/continuous source.  When the
    # catchlight analysis identifies the fill as passive ("Reflector fill",
    # "Ambient fill"), the light_count should be 1.  The reflector
    # produces a catchlight reflection but is not an independent source.
    _SINGLE_KEY_PATTERNS = {"loop", "rembrandt", "split", "broad", "short",
                            "window_portrait", "window_negative_fill"}
    if result.lighting_intel is not None:
        _sk_pat = result.authoritative_pattern
        _sk_lc = getattr(result.lighting_intel, "light_count", 0)
        if _sk_pat in _SINGLE_KEY_PATTERNS and _sk_lc > 1:
            _sk_ci = getattr(result.lighting_intel, "catchlight_intelligence", None) or {}
            _sk_fill_label = (_sk_ci.get("fill_source_label") or "").lower()
            _sk_passive = any(w in _sk_fill_label for w in ("reflector", "bounce", "ambient"))
            if _sk_passive:
                result.lighting_intel.light_count = 1

    # ── High-key / flat catchlight-based light_count floor ─────────────
    # High-key and flat setups inherently use multiple lights
    # (key + fill + background minimum).  When catchlights confirm
    # symmetric multi-source illumination (≥2 per eye), ensure the
    # light_count reflects this even if the lighting_inference under-counted.
    if result.lighting_intel is not None:
        _hk_pat = result.authoritative_pattern
        _hk_lc = getattr(result.lighting_intel, "light_count", 0)
        if _hk_pat in ("high_key", "flat") and _hk_lc < 2:
            # High-key and flat are definitionally multi-source setups.
            # High-key requires at minimum key + fill to produce the
            # near-shadowless wrap; flat requires even multi-directional
            # coverage.  When catchlights under-count (common in high-key
            # where specular highlights wash out catchlight detail), trust
            # the pattern classification — it already confirmed multi-source.
            result.lighting_intel.light_count = 2

    # ── High-key light_count cap when shadow evidence is absent ────────
    # In high-key setups with near-zero shadow density, multiple catchlights
    # are reflections of fill panels / white surfaces, not distinct directional
    # sources.  Cap at 2 (key + fill) when the shadow analysis confirms even
    # illumination (sd < 0.1).  This corrects over-counting from specular
    # reflections in beauty/editorial high-key lighting.
    _hk_cap_fired = False
    if result.lighting_intel is not None and result.authoritative_pattern == "high_key":
        _hk_cap_lc = getattr(result.lighting_intel, "light_count", 0)
        if _hk_cap_lc > 2 and _ls is not None:
            _hk_cap_sd = getattr(_ls, "shadow_density", 0.5)
            if _hk_cap_sd < 0.1:
                result.lighting_intel.light_count = 2
                _hk_cap_fired = True

    # ── Athletic rim sculpt: 2-strip minimum ───────────────────────────
    # Athletic rim sculpting physically requires at least 2 edge/strip
    # lights to create the bilateral rim effect.  Catchlight counting
    # under-reports because the lights are behind the subject.
    if result.lighting_intel is not None:
        if result.authoritative_pattern == "athletic_rim_sculpt":
            if getattr(result.lighting_intel, "light_count", 0) < 2:
                result.lighting_intel.light_count = 2

    # ── Clamshell: exactly 2 lights ─────────────────────────────────
    # Clamshell is *defined* by two lights: key above + fill below.
    # Catchlight counting under-reports when the fill light's catchlight
    # is too dim or occluded (common with beauty dishes / large softboxes
    # used as fill).  Over-reports when specular reflections from the
    # beauty dish surface or v-flats create extra catchlights.
    # The pattern resolution already confirmed clamshell, so the physics
    # require exactly 2.
    if result.lighting_intel is not None:
        if result.authoritative_pattern == "clamshell":
            result.lighting_intel.light_count = 2

    # ── Triangle: exactly 3 lights ──────────────────────────────────
    # Hurley triangle is defined by 3 positioned lights (key + 2 fills).
    # VLM over-counts (reports 4) because it reads the bright background
    # as a separate source.  The triangle upgrade already confirmed the
    # 3-point setup; force the physically correct count.
    if result.lighting_intel is not None:
        if result.authoritative_pattern == "triangle":
            result.lighting_intel.light_count = 3

    # ── Ring light: always a single source ────────────────────────────
    # Ring lights produce multiple catchlight reflections (the ring
    # shape creates several bright spots) but it's physically one light.
    # Override any over-counted light_count.
    if result.lighting_intel is not None:
        if result.authoritative_pattern == "ring_light":
            result.lighting_intel.light_count = 1

    # ── Catchlight-inferred light_count floor (final pass) ───────────
    # The basic vision pass infers light count directly from distinct
    # catchlight positions across both eyes and stores the result in
    # vision_data["catchlights"]["inferred"]["lightCount"].  This count
    # is authoritative for lights that cast no visible face shadow —
    # hair lights, rim lights, background fill — which the shadow-geometry
    # pipeline systematically under-counts.
    # Placed AFTER all pattern-specific caps so that ring_light (always 1)
    # and over-counting corrections are already applied before this floor.
    # Guards:
    #   - Exempt ring_light: the ring cap above already set count=1 and
    #     a ring's circular reflection pattern creates many false catchlights.
    #   - Cap inferred count at 4: no common portrait setup uses more than
    #     4 distinct sources; higher counts indicate specular noise.
    #   - Only raises (never lowers) the count.
    #   - Only fires when the shadow pipeline already confirmed >= 2 sources
    #     (current_lc >= 2).  Single-light portraits (rembrandt_classic,
    #     broad, etc.) return current_lc=1 and must not be boosted by
    #     false catchlights from specular noise in high-contrast images.
    #     Multi-source setups (hurley_triangle: key + fill + hair/rim)
    #     return current_lc=2 and ARE eligible — the hair light that casts
    #     no face shadow is correctly picked up here.
    if result.lighting_intel is not None and result.vision_data:
        _cl_auth = result.authoritative_pattern
        # Exempt patterns whose light count was set by definitive signature
        # or pattern-specific caps — the catchlight floor must not override
        # physics-grounded or cap-corrected counts.
        # high_key: capped at 2 above (specular reflections ≠ sources)
        # clamshell: fixed at 2 above (2-light definition)
        _cl_exempt = {"ring_light", "high_key", "clamshell", "flat"} | set(_DEFINITIVE_LIGHT_COUNTS.keys())
        if _cl_auth not in _cl_exempt:
            _cl_raw = result.vision_data.get("catchlights", {})
            _cl_inferred = _cl_raw.get("inferred", {}) if isinstance(_cl_raw, dict) else {}
            _cl_inferred_count = _cl_inferred.get("lightCount") if isinstance(_cl_inferred, dict) else None
            if isinstance(_cl_inferred_count, int) and 0 < _cl_inferred_count <= 4:
                _cur_lc = getattr(result.lighting_intel, "light_count", 0)
                if _cl_inferred_count > _cur_lc >= 2:
                    result.lighting_intel.light_count = _cl_inferred_count

    # ── VLM background light floor ───────────────────────────────────
    # CV has no signal for deliberate background lights — they cast no
    # face shadows and their catchlights are behind or outside the iris.
    # When VLM explicitly confirms a background light, raise light_count
    # by 1.  Guards: only raises (never lowers); exempt ring_light (single
    # source by physics); cap at 4 (applied by sanity cap below).
    if result.lighting_intel is not None and result.vlm_description is not None:
        _vlm_bg_sigs = getattr(result.vlm_description, "signals", None)
        _vlm_recon_bg = getattr(_vlm_bg_sigs, "reconstruction", None) \
            if _vlm_bg_sigs else None
        _vlm_bg_light = getattr(_vlm_recon_bg, "background_light_present", None) \
            if _vlm_recon_bg else None
        # Exempt patterns with fixed/capped light counts — the VLM bg bump
        # should not override floor/cap logic that already set the correct count.
        # High_key: exempt when sd-based cap fired (specular noise) OR when
        # sd < 0.1 (same threshold as cap — even if count was already ≤ 2).
        _hk_sd_low = (
            result.authoritative_pattern == "high_key"
            and _ls is not None
            and getattr(_ls, "shadow_density", 0.5) < 0.1
        )
        _bg_exempt = result.authoritative_pattern in ("ring_light", "clamshell", "flat", "triangle") or _hk_cap_fired or _hk_sd_low
        if _vlm_bg_light is True and not _bg_exempt:
            _cur_lc_bg = getattr(result.lighting_intel, "light_count", 0)
            if 0 < _cur_lc_bg < 4:
                result.lighting_intel.light_count = _cur_lc_bg + 1
                logger.debug(
                    "[vlm_bg_light] background_light_present → light_count %d → %d",
                    _cur_lc_bg, result.lighting_intel.light_count,
                )

    # ── General light_count sanity cap ─────────────────────────────────
    # No common portrait setup uses more than 4 distinct light sources.
    # When lighting_intel reports > 4, it's almost always false positives
    # from specular reflections (BW blown highlights, jewelry, glasses).
    # Prefer cue_inference geometry count (deduped reflection_architecture,
    # floor reflections removed, nearby positions grouped) when available;
    # otherwise hard cap at 4.
    if result.lighting_intel is not None:
        _cap_lc = getattr(result.lighting_intel, "light_count", 0)
        if _cap_lc > 4:
            _cap_geo = None
            if isinstance(result.cue_inference_result, dict):
                _cap_geo = result.cue_inference_result.get("geometry")
            _cap_geo_lc = getattr(_cap_geo, "light_count_estimate", 0) if _cap_geo else 0
            if 0 < _cap_geo_lc <= 4:
                result.lighting_intel.light_count = _cap_geo_lc
            else:
                result.lighting_intel.light_count = 4

    # ── Pattern-based direction enrichment ──────────────────────────
    # Some patterns have inherent key positions.  When the catchlight
    # and shadow pipelines both failed to resolve a direction, use the
    # authoritative pattern as a weak signal.
    _PATTERN_IMPLIED_SIDE = {
        "butterfly": "center",   # overhead / on-axis
        "clamshell": "center",   # overhead key + below fill
    }
    if result.lighting_intel is not None:
        _intel = result.lighting_intel
        if getattr(_intel, "key_side", "unknown") == "unknown":
            implied = _PATTERN_IMPLIED_SIDE.get(pc.authoritative_pattern)
            if implied:
                _intel.key_side = implied

    # ── Key direction reconciliation: shadow geometry vs VLM ─────────
    # When cue_inference shadow geometry gives a confident directional
    # read that conflicts with the VLM's key_side, prefer the shadow
    # geometry.  Shadow direction is physics-based (nose shadow centroid
    # displacement) while VLM key_side is a semantic guess that can be
    # wrong when the subject faces away from the key (window light,
    # short lighting setups where the lit side isn't camera-facing).
    # Only fire when:
    #   - cue_inference geometry has a definite direction (not "unknown")
    #   - cue_inference confidence >= 0.5 (reliable geometry)
    #   - the two sides genuinely conflict (left vs right)
    # Measured: window_soft_side has VLM key_side="left" but shadow
    #   geometry key_dir="upper_right" → window clearly on camera-right.
    if result.lighting_intel is not None:
        _rk_intel = result.lighting_intel
        _rk_vml_side = getattr(_rk_intel, "key_side", "unknown") or "unknown"
        _rk_geo = None
        if isinstance(result.cue_inference_result, dict):
            _rk_geo = result.cue_inference_result.get("geometry")
        _rk_geo_dir = getattr(_rk_geo, "key_light_direction", "unknown") if _rk_geo else "unknown"
        _rk_geo_conf = getattr(_rk_geo, "confidence", 0.0) if _rk_geo else 0.0
        if _rk_geo_dir and _rk_geo_dir != "unknown" and _rk_geo_conf >= 0.5:
            # Normalize both to left/right for conflict check
            _rk_vml_lr = "left" if "left" in _rk_vml_side else ("right" if "right" in _rk_vml_side else "")
            _rk_geo_lr = "left" if "left" in _rk_geo_dir else ("right" if "right" in _rk_geo_dir else "")
            if _rk_vml_lr and _rk_geo_lr and _rk_vml_lr != _rk_geo_lr:
                logger.info(
                    "[key_reconcile] shadow geometry '%s' (conf=%.2f) overrides "
                    "VLM key_side '%s' — physics over semantics",
                    _rk_geo_dir, _rk_geo_conf, _rk_vml_side,
                )
                _rk_intel.key_side = _rk_geo_dir

    # ── Sync truth source: authoritative_pattern → all cards ─────────
    # pattern_candidates is the single source of truth.  Write it back to
    # lighting_read.shadow_pattern and recreation_setup.setup_family so
    # the Analysis card, Blueprint, and Shoot Mode steps never disagree.
    _sync_authoritative_pattern_to_cards(result)

    # ── Perception layer (read-only diagnostics) ─────────────────────
    _compute_perception_layer(result)

    _total = round(_time.perf_counter() - _t0, 2)
    result.stage_timings["total"] = _total
    logger.info("[analyze_image] TOTAL: %.1fs", _total)

    # ── Phase L1 observability ────────────────────────────────────────
    try:
        from engine.observability import emit_analysis_l1
        emit_analysis_l1(result)
    except Exception:
        logger.debug("L1 observability emission failed", exc_info=True)

    # ── Sentry: finish transaction with final tags ─────────────────────
    if _txn:
        try:
            _txn.set_tag("pattern", result.authoritative_pattern or "unknown")
            _txn.set_tag("confidence", result.pattern_confidence_label or "unknown")
            _txn.set_tag("source", result.authoritative_pattern_source or "unknown")
            _txn.set_data("stage_timings", result.stage_timings)
            _txn.finish()
        except Exception:
            pass

    return result



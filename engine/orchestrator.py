def _apply_specialty_pattern(result: "AnalysisResult") -> Optional[str]:
    """Check environmental/tonal/structural signals to upgrade a geometric
    pattern to a specialty pattern name.

    Returns the specialty pattern name if conditions are met, else None.
    The caller keeps the geometric pattern as a fallback.

    Specialty patterns are contextual overlays on top of standard geometric
    patterns.  A "rembrandt" shadow from a window is "window_portrait";
    a "split" in a dark studio with extreme contrast is "low_key".

    CONSERVATIVE: only fires when multiple independent signals converge.
    A single signal (e.g. dark background alone) is never sufficient.

    -- Corrective vs. Context layering (analysis-order doctrine) ----------
    Branches in this function fall into two groups, in this order:
      1. CORRECTIVE -- fix a base pattern that is physically inconsistent
         with pose / BW / clamshell / bilateral geometry (e.g. golden_hour
         without a face -> tabletop_soft_product).  These run FIRST.
      2. CONTEXT -- layer source_context / specialty overlays on top of a
         geometric base (window_portrait, overcast->flat, golden_hour returns
         the base geometry, editorial_rim_key, window_negative_fill, etc.).
         These run AFTER correction and NEVER invent a new geometric key.
    source_context values (golden_hour, overcast, window_light) must never
    leave this function as the authoritative pattern name -- they either
    return the underlying geometry or map to an already-canonical specialty
    (window_portrait, flat).  Pattern naming comes from geometry; context
    rides on top of it.
    """
    base = result.authoritative_pattern
    if not base:
        return None

    cr = result.cue_report
    cls = result.classification
    ci = result.cue_inference_result  # dict with geometry, environment, etc.
    li = result.lighting_intel

    if not cr or not cls:
        return None

    # Face presence check — available before FaceValidation is computed.
    # Used to guard specialty rules that only apply to portrait/human subjects.
    _face_box = result.debug_data.get("face_box") if result.debug_data else None
    _face_detected = _face_box is not None
    # Secondary: region_attribution face detection score
    if not _face_detected and result.vision_data:
        _ra = result.vision_data.get("region_attribution", {})
        _fds = _ra.get("face_detection_score")
        if _fds is not None and _fds > 0.5:
            _face_detected = True

    # Guard: cue_report must be a real VisualCueReport, not a dict stub
    if not hasattr(cr, "contrast_ratio"):
        return None

    env = ci.get("environment") if ci else None
    geo = ci.get("geometry") if ci else None

    brightness = (cls.get("brightness") or "").lower()
    cr_label = ""
    if cr.contrast_ratio:
        cr_label = getattr(cr.contrast_ratio, "label", "").lower()
    bg = cr.background_illumination
    bg_bright = bg and getattr(bg, "brightness_relative", "") == "brighter" if bg else False
    bg_dark = bg and getattr(bg, "brightness_relative", "") in ("darker", "dark") if bg else False
    mood = (cls.get("mood") or "").lower()

    # ── Correction: golden_hour without face ─────────────────────────
    # When reference_read classifies a scene as golden_hour but no face
    # is detected, the classification may be a product/tabletop shot with
    # warm tones that triggered the outdoor-sun heuristic.  Override the
    # prior golden_hour classification with a more appropriate specialty
    # (tabletop_soft_product) when all of the following hold:
    #   1. Current base is already "golden_hour" (set by reference_read)
    #   2. No face detected
    #   3. Mood is not outdoors/portrait (editorial, commercial, or neutral)
    # This returns early so the rest of the specialty rules are skipped.
    if base == "golden_hour" and not _face_detected:
        _gh_mood = mood in ("editorial", "commercial", "natural", "")
        if _gh_mood:
            # Use tabletop_soft_product as the correction for no-face warm shots
            return "tabletop_soft_product"

    # ── High-key: mood classifier says "high_key" ───────────────────
    # Pixel-level brightness/contrast are unreliable for high-key detection
    # (high-key images can have extreme pixel contrast from dark hair against
    # white bg, and the bg_relative may report "similar" instead of "brighter").
    # The mood classifier is the most reliable signal — when it explicitly
    # classifies the mood as "high_key", trust that.
    # Only apply when a geometric base pattern was detected (not "unknown") —
    # high-key images are well-lit studio shots that should have identifiable
    # shadow patterns.  base="unknown" + mood=high_key is more likely
    # flat/commercial lighting where no dominant key shadow is visible.
    bg_pat = getattr(bg, "pattern", "") if bg else ""
    # "clamshell" is excluded by default: it describes a specific 2-source
    # geometric setup (key above + fill below) that is orthogonal to the
    # high_key tonal descriptor.  Exception: a clamshell that is also
    # classified as high_key mood AND has very_high overall brightness is
    # "clamshell" is excluded: it describes a specific 2-source geometric
    # setup (key above + fill below) that is orthogonal to the high_key
    # tonal descriptor.  The fallback (bg_bright + count≥4) below handles
    # beauty-dish high-key cases where mood="editorial".
    # Shadow density guard: high_key lighting produces minimal directional
    # shadows.  If the light_structure measured significant shadow density
    # (> 0.20) or strong LR asymmetry (> 0.20), the lighting is directional
    # and shouldn't be overridden to high_key regardless of mood/brightness.
    _ls_data = getattr(cr, "light_structure", None) if cr else None
    _hk_shadow_den = getattr(_ls_data, "shadow_density", 0.0) if _ls_data else 0.0
    _hk_lr_asym = getattr(_ls_data, "left_right_asymmetry", 0.0) if _ls_data else 0.0
    # Off-axis key: light_structure pattern_name reflects key position from
    # nose-shadow centroid displacement.  When it says "loop", "rembrandt",
    # "split", "broad", or "short", the key is positioned off-axis — a
    # directional signal even when fill lights wrap shadows below the 0.20
    # threshold.
    _hk_ls_pattern = getattr(_ls_data, "pattern_name", "") if _ls_data else ""
    _hk_offaxis_key = _hk_ls_pattern in ("loop", "rembrandt", "split", "broad", "short")
    # Also check cue_inference geometry's shadow_pattern as a secondary signal.
    # light_structure can return "clamshell" for Hurley-triangle setups (even
    # 4-light wrap looks clamshell-like geometrically) even when the actual
    # key is offset.  geo.shadow_pattern from infer_geometry() adds a more
    # robust directional check — when it says "loop", the key IS off-axis.
    _hk_geo_pattern = getattr(geo, "shadow_pattern", "") if geo else ""
    _hk_offaxis_geo = _hk_geo_pattern in ("loop", "rembrandt", "split", "broad", "short")
    # When shadow signals are both below 0.15, the off-axis "loop" from
    # light_structure is likely noise-centroid on near-zero shadow.  In that
    # case, trust only the shadow_den / lr_asym thresholds, not the off-axis
    # pattern name.  This prevents a noise-loop from blocking high_key
    # promotion when the mood classifier explicitly says high_key.
    _hk_offaxis_reliable = (
        _hk_shadow_den > 0.15 or _hk_lr_asym > 0.15
    )
    _hk_has_directional_shadow = (
        _hk_shadow_den > 0.20 or _hk_lr_asym > 0.20
        or ((_hk_offaxis_key or _hk_offaxis_geo) and _hk_offaxis_reliable)
    )

    # 3-light setup guard: exactly 3 distinct catchlights = a deliberate
    # triangle / 3-point / Hurley-triangle setup.  This is NOT high-key —
    # high-key requires diffuse wrap from many soft sources, not 3 positioned
    # sources.  Guard both high-key checks so a Hurley triangle butterfly
    # base is not promoted to high_key.
    # Consistent with the clamshell ≥ 4 guard below: count=3 means triangle,
    # count ≥ 4 means high-key specular inflation.
    # Uses deduped catchlight count from reflection_architecture when
    # available (same source as the loop/rembrandt → triangle upgrade).
    _hk_li_count = getattr(li, "light_count", 0) if li else 0
    _hk_li_conf = getattr(li, "pattern_confidence", 0.0) if li else 0.0
    _hk_ra = getattr(cr, "reflection_architecture", None) if cr else None
    _hk_deduped = getattr(_hk_ra, "total_catchlights", None)
    if _hk_deduped is not None:
        _hk_li_count = _hk_deduped
    _is_3light_setup = _hk_li_count == 3 and _hk_li_conf >= 0.5
    # Multi-light offaxis guard: 4+ lights with a directional key from
    # light_structure is a multi-point portrait setup (e.g. Hurley triangle
    # + rim), not high-key.  Multiple lights can drive shadow metrics to
    # near-zero by filling each other's shadows, making the standard shadow
    # guard ineffective.  Trust the CV pattern_name directional signal
    # even when shadow_den/lr_asym are zero.
    # Uses RAW li.light_count, not deduped catchlight count — rim/hair
    # lights don't produce catchlights but still indicate a multi-point
    # setup.  The deduped count can collapse 4 sources to 1.
    # Measured: hurley_triangle has li.light_count=3 (raw, before post-
    #   processing bumps to 4), ra.total_catchlights=1,
    #   ls.pattern_name="loop", shadow_den=0.0, lr_asym=0.0.
    _hk_raw_count = getattr(li, "light_count", 0) if li else 0
    _is_multi_offaxis = _hk_raw_count >= 3 and _hk_offaxis_key

    # Pre-compute fill ratio for overfill guard (used by multiple paths).
    _hk_fr = getattr(cr, "fill_ratio", None) if cr else None
    _hk_fr_ratio = getattr(_hk_fr, "ratio", 0.0) if _hk_fr else 0.0
    # Overfill guard: shadow_density near zero + fill_ratio extreme = multi-source
    # flat with too much fill, not high_key.  Mood classifier can confuse the
    # two because both have even wrap, but overfill retains a neutral background
    # while high_key has a blown-white one.
    # Tight thresholds: sd ≤ 0.03 (near-zero shadow) + fr > 0.95 (near-unity
    # fill).  Broader thresholds (0.05/0.90) catch legitimate directional
    # patterns like broad (sd=0.031, fr=0.914).
    # Additional signal: non-bright background distinguishes overfill from
    # genuine high-key (which requires blown-white background).
    # Measured: overfill_flat sd=0.03, fr=0.98, bg_bright=False.
    #   broad sd=0.031, fr=0.914 — correctly excluded by tight thresholds.
    _hk_is_overfill = (
        _hk_shadow_den <= 0.03 and _hk_fr_ratio > 0.95
        and not bg_bright
    )
    # Overfill images should become "flat" — they're multi-source setups
    # where fill cancels all key shadows.  Without this, the base pattern
    # (often butterfly or loop from the shadow-less geometry) persists.
    if _hk_is_overfill and base in ("butterfly", "loop", "unknown"):
        return "flat"

    # When mood classifier explicitly says high_key, use a LENIENT shadow
    # guard — mood=high_key is a strong signal (VLM + brightness + palette).
    # Raised thresholds: shadow_den > 0.35 or lr_asym > 0.50 (was 0.20).
    # The standard guard is too strict: a slightly turned head in a high-key
    # setup can produce lr_asym 0.30-0.45 without being directional lighting.
    _hk_mood_directional = (_hk_shadow_den > 0.35 or _hk_lr_asym > 0.50)
    if mood == "high_key" and base not in ("unknown", "clamshell") \
       and not _is_3light_setup and not _is_multi_offaxis:
        if not _hk_mood_directional and not _hk_is_overfill:
            return "high_key"

    # ── High-key fallback: bright background + high brightness + soft base
    # When the mood classifier says "editorial" or "beauty" instead of
    # "high_key" but the image is clearly high-key (bright background +
    # high overall brightness), promote to high_key.  Only fire for
    # soft base patterns compatible with multi-light high-key setups —
    # exclude dramatic/directional patterns (rembrandt, triangle, split,
    # short) AND flat_fashion.  flat_fashion is excluded because white-
    # seamless catalog shots naturally have bright backgrounds — they are
    # on-axis even studio setups, not high-key scene lighting.  Promoting
    # flat_fashion to high_key produces systematic false positives on
    # catalog/product portraits.
    # Shadow density guard: directional shadows contradict high-key.
    # 3-light guard: count=3 → triangle setup, not diffuse high-key.
    if bg_bright and brightness in ("high", "very_high") and \
       base in ("loop", "butterfly", "broad") and not _hk_has_directional_shadow \
       and not _is_3light_setup and not _is_multi_offaxis:
        return "high_key"
    # Clamshell in a bright high-key environment (beauty dish editorial) can
    # also be high_key, but requires an additional guard: the catchlight
    # light_count must be ≥ 4.  Count=3 indicates a genuine triangle setup
    # (three distinct sources), not high-key specular inflation.  Count ≥ 4
    # in a bright+bg_bright clamshell scene reliably indicates over-counted
    # reflections from fill panels — a high-key signature.
    # Directional key guard: if light_structure pattern_name is a directional
    # pattern (loop, rembrandt, etc.) the clamshell base came from the
    # butterfly→clamshell upgrade, not from a true on-axis clamshell setup.
    # A Hurley-triangle with 4 lights and offset key should not become high_key.
    if bg_bright and brightness in ("high", "very_high") and base == "clamshell":
        _ck_lc = getattr(li, "light_count", 0) if li else 0
        if _ck_lc >= 4 and not _hk_offaxis_key:
            return "high_key"

    # ── High-key pixel-evidence fallback ──────────────────────────────
    # When the mood classifier misses (says "editorial"/"beauty" instead
    # of "high_key") AND the base is clamshell (excluded from mood path),
    # pure pixel evidence can still identify high_key: bright background +
    # near-zero shadow + high fill ratio.  These three signals together
    # are physically diagnostic of multi-source even wrap lighting.
    # Note: _is_3light_setup guard is NOT applied here.  The base check
    # already excludes "triangle" — if base is clamshell/butterfly/flat,
    # the triangle definitive signature was not set, so the 3-catchlight
    # deduplication likely counted fill panel reflections, not 3 distinct
    # sources.  The shadow + fill signals are stronger diagnostic evidence
    # than the catchlight dedup count.
    if bg_bright and base in ("clamshell", "butterfly", "flat") \
       and _hk_shadow_den < 0.06:
        if _hk_fr_ratio > 0.95:
            return "high_key"

    # ── Flat fashion: unknown pattern + bright + even background ──────
    # Flat fashion / catalog lighting produces minimal directional shadows.
    # The geometric classifier returns "unknown" (no clear key direction)
    # but the image is bright with an even, controlled background.
    if base == "unknown" and brightness in ("high", "very_high", "medium"):
        bg_even_or_similar = bg_pat == "even" or (
            bg and getattr(bg, "brightness_relative", "") == "similar"
        )
        if bg_even_or_similar:
            # Ensure it's not outdoor — flat (from flat_fashion) is primarily a studio concept
            is_natural = getattr(env, "is_natural_light", False) if env else False
            if not is_natural:
                return "flat"

    # ── Low-key: handled by cue_inference when key_direction="unknown" ──
    # NOT applied as a post-classification specialty because the signals
    # (extreme contrast + dark background + dramatic mood) are too common
    # in standard studio portraits.  The cue_inference low_key detection
    # at _infer_shadow_pattern() line 344 is the reliable path.

    # ── Window portrait: indoor natural light + soft source ───────────
    # Requires: indoor environment + natural light + no studio catchlights.
    # "outdoor_shade" is excluded — that's outdoor, not window.
    if env and getattr(env, "is_natural_light", False):
        env_type = getattr(env, "environment_type", "")
        special = getattr(env, "special_cases", []) or []

        if env_type == "indoor_ambient" and "direct_sunlight" not in special:
            # Must also be soft light (window light is inherently soft/diffused)
            sq = ci.get("source_quality") if ci else None
            modifier_fam = getattr(sq, "key_modifier_family", "") if sq else ""
            seh = cr.shadow_edge_hardness
            is_soft = (modifier_fam in ("softbox", "umbrella", "window") or
                       (seh and getattr(seh, "classification", "") == "soft"))
            if is_soft and base in ("loop", "rembrandt", "broad", "short"):
                return "window_portrait"

    # ── Golden hour: warm CCT + outdoor sun ───────────────────────────
    # Primary: actual detected warm CCT + outdoor sun.
    # Fallback: when CCT detection is unavailable, use converging warm
    # color hints from the environmental shadow analysis.  The combination
    # of natural light + outdoor + warm_overall + bg=brighter (sun behind
    # subject) is unique to golden hour across the full benchmark set.
    if env and getattr(env, "is_natural_light", False):
        env_type = getattr(env, "environment_type", "")
        cct = getattr(li, "detected_cct_kelvin", None) if li else None
        # Guard: require face detection for golden_hour classification.
        # Warm-toned product shots (no face) can trigger CCT < 4000K and
        # outdoor_sun misclassification — requiring a face prevents these
        # false positives while preserving accurate golden_hour on portraits.
        if cct and cct < 4000 and env_type == "outdoor_sun" and _face_detected:
            # Golden hour is a source context, not a geometry.
            # Return the detected geometry base; fall back to loop (warm directional default).
            if base and base not in ("unknown", ""):
                return base
            # Geometry was not resolved — fall back to warm directional default.
            # Flag this so observability can surface it as an ambiguity.
            result.notes.append("golden_hour_geometry_fallback")
            return "loop"
        # Fallback: warm color convergence without explicit CCT
        if env_type in ("outdoor_sun", "outdoor_shade") and bg_bright and _face_detected:
            _esc = getattr(cr, "environmental_shadow_continuity", None)
            _env_hints = getattr(_esc, "environment_hints", []) if _esc else []
            if "warm_overall" in _env_hints and "warm_background" in _env_hints:
                if base and base not in ("unknown", ""):
                    return base
                # Geometry was not resolved — fall back to warm directional default.
                result.notes.append("golden_hour_geometry_fallback")
                return "loop"

    # ── Golden hour env-agnostic fallback ─────────────────────────────
    # When the env classifier misidentifies warm natural backlight as
    # studio/unknown (is_natural_light=False), the warm_overall +
    # warm_background + bg_bright convergence still reliably identifies
    # golden-hour character.  Restrict to diffuse base patterns only —
    # hard-edge patterns (split, rembrandt) would indicate a studio key,
    # not a golden-hour wash, even with warm environment hints.
    #
    # Guard: require a detected face OR confirmed natural light.
    # Without a face (e.g. product/tabletop shots with warm backgrounds),
    # warm_overall + warm_background signals are too ambiguous to reliably
    # indicate golden hour vs. warm-toned studio/product lighting.
    # This prevents tabletop product shots with warm surfaces from being
    # misclassified as golden_hour.
    _env_is_natural = env and getattr(env, "is_natural_light", False)
    if bg_bright and base in ("butterfly", "loop", "broad", "short"):
        if _face_detected or _env_is_natural:
            _esc2 = getattr(cr, "environmental_shadow_continuity", None)
            _hints2 = getattr(_esc2, "environment_hints", []) if _esc2 else []
            if "warm_overall" in _hints2 and "warm_background" in _hints2:
                # base is already gated to ("butterfly", "loop", "broad", "short")
                return base

    # ── Overcast natural: outdoor shade + soft + low contrast ─────────
    # Requires: outdoor_shade + soft shadows + low contrast.
    # Exception: when "high_contrast_grade" is in special_cases, the pixel
    # contrast reflects post-processing, not the actual lighting.  In that
    # case, relax the contrast requirement — BUT require medium+ brightness
    # to distinguish overcast (diffused outdoor light, even exposure) from
    # intentional dramatic setups (dark mood, low brightness) that also
    # happen to be outdoors with graded contrast.
    if env and getattr(env, "is_natural_light", False):
        env_type = getattr(env, "environment_type", "")
        special = getattr(env, "special_cases", []) or []
        if env_type == "outdoor_shade" and "direct_sunlight" not in special:
            seh = cr.shadow_edge_hardness
            is_soft = seh and getattr(seh, "classification", "") == "soft"
            contrast_ok = cr_label in ("low", "very_low")
            graded = "high_contrast_grade" in special and brightness in (
                "medium", "high", "very_high",
            )
            if is_soft and (contrast_ok or graded):
                # Overcast is source context (→ source_context=overcast).
                # When the base pattern has significant directionality
                # (lr_asym > 0.18), the overcast light is coming from one
                # side (e.g. open sky from camera-right) and the base
                # geometry (broad, loop, rembrandt) should be preserved.
                # Only promote to flat when the light is truly non-directional.
                # Measured: overcast_natural lr=0.25 → directional, keep broad.
                _oc_lr = getattr(_ls_data, "left_right_asymmetry", 0.0) if _ls_data else 0.0
                if _oc_lr > 0.18 and base in ("broad", "loop", "rembrandt", "short"):
                    return base  # directional overcast → preserve geometry
                return "flat"

    # ── Editorial rim key: loop + no face shadow + very narrow highlight ──
    # Edge/rim lighting from behind creates a sliver of highlight on the
    # face edge with NO nose/cheek shadows (light doesn't reach the front).
    # Distinctive signal combination: loop or butterfly geometry (butterfly
    # fires when the face front is uniformly dark and appears symmetric to
    # the shadow analyzer) + shadow_density ≈ 0 + highlight width < 15%.
    # P1b: accept both "loop" and "butterfly" as base — rim-key images with
    # a fully shadowed face front produce either base depending on the face
    # orientation; the highlight_width + shadow_density signals are the
    # authoritative discriminators regardless of base.
    if base in ("loop", "butterfly") and bg_dark and brightness == "low":
        ls_data = getattr(cr, "light_structure", None) if cr else None
        _erk_hl = getattr(ls_data, "highlight_width_ratio", 1.0) if ls_data else 1.0
        _erk_sd = getattr(ls_data, "shadow_density", 0.5) if ls_data else 0.5
        if _erk_hl < 0.15 and _erk_sd < 0.02:
            return "editorial_rim_key"

    # ── Ring light: DONUT catchlights + symmetric base ──────────────
    # Ring lights produce a distinctive DONUT-shaped catchlight — a bright
    # ring with a dark center (hollow).  This is the defining visual
    # signature.  Only upgrade to ring_light when the catchlight shape
    # is specifically "ring" (hollow center confirmed by vision_pipeline),
    # NOT merely "round".  Round catchlights come from beauty dishes,
    # octaboxes, round softboxes — many modifiers produce solid round
    # reflections.  Requiring "ring" (donut) eliminates false ring_light
    # classifications on butterfly/clamshell setups with beauty dishes.
    #
    # P1a: exclude images with very narrow highlights (highlight_width < 0.15).
    # Ring lights produce broad, symmetric illumination across the face;
    # a narrow highlight sliver indicates a hard off-axis backlight (rim key),
    # not a ring — even if the specular catchlight looks circular.
    cs = getattr(cr, "catchlight_shape", None)
    _cs_shape = getattr(cs, "dominant_shape", "") if cs else ""
    # Also check shapes_seen list — the dominant_shape aggregation can
    # report "round" even when individual eyes both show "ring" (donut).
    # If "ring" appears in shapes_seen, treat it as a ring detection.
    _cs_seen = getattr(cs, "shapes_seen", []) if cs else []
    _has_ring = _cs_shape == "ring" or "ring" in _cs_seen
    # Secondary: check raw CV catchlight data directly — both eyes must
    # show "ring" shape for high confidence.
    if not _has_ring and result.vision_data:
        _cv_cl = result.vision_data.get("catchlights", {}) if isinstance(result.vision_data, dict) else {}
        _cv_cls = _cv_cl.get("catchlights", []) if isinstance(_cv_cl, dict) else []
        _ring_eyes = sum(1 for c in _cv_cls if isinstance(c, dict) and c.get("shape") == "ring")
        if _ring_eyes >= 2:
            _has_ring = True
    if _has_ring:
        # Ring light requires donut-shaped catchlights (bright ring with dark
        # center).  "Round" (solid circle) catchlights are NOT sufficient —
        # beauty dishes, octaboxes, and many modifiers also produce round
        # reflections.  Only the hollow donut shape is definitively ring.
        is_natural = getattr(env, "is_natural_light", False) if env else False
        ls_data = getattr(cr, "light_structure", None) if cr else None
        _rl_hl = getattr(ls_data, "highlight_width_ratio", 1.0) if ls_data else 1.0
        if not is_natural and _rl_hl >= 0.15 and base in ("broad", "butterfly", "loop", "clamshell"):
            return "ring_light"

    # ── Ring light symmetry fallback: no donut shape detected ─────────
    # When the vision pipeline can't detect the donut shape (common in
    # B&W images or tight crops), use physical symmetry signatures.
    # Ring lights are uniquely characterized by:
    #   - Butterfly/clamshell base (on-axis centered key)
    #   - Near-zero LR asymmetry (< 0.05) — more symmetric than any
    #     other modifier because light comes from ALL directions equally
    #   - High fill ratio (> 0.95) — uniform wrap from ring geometry
    #   - On-axis catchlight at 12 o'clock
    # These signals together are diagnostic — no other modifier produces
    # this combination.  Beauty dishes have lr > 0.05, softboxes have
    # lower fill ratio, and butterfly setups from a single overhead key
    # produce more shadow (sd > 0.20).
    # Measured: ring_light lr=0.019, fr=0.985, sd=0.183, catchlight=12.
    #   butterfly lr=0.433 → excluded by lr threshold.
    if not _has_ring and base in ("butterfly", "clamshell"):
        _rl_ls = getattr(cr, "light_structure", None) if cr else None
        _rl_lr = getattr(_rl_ls, "left_right_asymmetry", 1.0) if _rl_ls else 1.0
        _rl_fr = getattr(cr, "fill_ratio", None) if cr else None
        _rl_fr_ratio = getattr(_rl_fr, "ratio", 0.0) if _rl_fr else 0.0
        _rl_ci = getattr(li, "catchlight_intelligence", None) or {}
        _rl_pk = _rl_ci.get("primary_key") or {}
        _rl_pk_pos = _rl_pk.get("position", "")
        # On-axis: 11, 12, or 1 o'clock — all near-overhead positions
        # consistent with ring light centered above/around the camera.
        # Extract hour number to avoid substring false matches
        # ("1 " would match "11 o'clock").
        try:
            _rl_hour = int(_rl_pk_pos.split()[0])
        except (ValueError, IndexError):
            _rl_hour = None
        _rl_on_axis = _rl_hour in (11, 12, 1)
        _rl_natural = getattr(env, "is_natural_light", False) if env else False
        # Clamshell guard: when catchlights show distinct key+fill sources
        # (fill_bilateral or light_count_from_catchlights >= 2), the setup
        # is clamshell (key above + fill below), not ring_light.  Ring light
        # produces a single ring reflection, not separate key/fill catchlights.
        _rl_bilateral = _rl_ci.get("fill_bilateral", False)
        _rl_cl_count = _rl_ci.get("light_count_from_catchlights", 0)
        _rl_is_clamshell = _rl_bilateral or _rl_cl_count >= 2
        if (_rl_lr < 0.05 and _rl_fr_ratio > 0.95 and _rl_on_axis
                and not _rl_natural and not _rl_is_clamshell):
            return "ring_light"

    # ── Low-key: mood=low_key + dramatic base + very narrow highlight ──
    # Low-key lighting is a high-ratio setup where most of the face is in
    # shadow with only a narrow sliver of light.  mood=low_key alone is
    # too common (fires on 12/32 benchmarks), so we require:
    #   - mood explicitly classified as "low_key"
    #   - base pattern is a dramatic or ambiguous geometric type; loop and
    #     butterfly are included because low-confidence classifiers often
    #     return these when the true setup is low-key but shadow geometry
    #     is ambiguous (e.g. very dark face with single side key).
    #   - dark background (intentional)
    #   - very narrow highlight width (< 15%) OR high shadow density (> 30%)
    #     — either confirms a high-ratio setup; hl_w alone can be unreliable
    #     when the classifier returns 1.0 (fallback) for images without a
    #     detected face mesh.
    if mood == "low_key" and base in ("split", "rembrandt", "loop", "butterfly") and bg_dark:
        ls_data = getattr(cr, "light_structure", None) if cr else None
        hl_w = getattr(ls_data, "highlight_width_ratio", 1.0) if ls_data else 1.0
        if hl_w < 0.15:
            return "low_key"

    # ── No-face low-key: product shots on dark background ─────────────
    # When no face is detected, the highlight_width_ratio guard above is
    # unreliable — it's computed from the full image, not a face crop.
    # For no-face images, mood=low_key + dark background + low brightness
    # is sufficient evidence.  This catches product photography (backlit
    # bottles, still life on black) where the shadow geometry classifiers
    # default to "loop" but the actual setup is low-key studio lighting.
    # Measured: bottle_backlight has mood=low_key, brightness=low,
    #   bg=darker, controlled_background, no face → hl_w=0.288 (global).
    if mood == "low_key" and not _face_detected and bg_dark \
       and brightness == "low":
        return "low_key"

    # ── Cinematic extreme low-key: mood="cinematic" + extreme shadow ──
    # Extreme low-key fashion/editorial shots often have mood="cinematic"
    # (not "low_key") because the VLM reads the dramatic intent rather than
    # the lighting technique.  When shadow_density > 0.85, the shadow
    # geometry classifiers are unreliable — there's so little lit area that
    # they default to loop/butterfly/split based on minimal visible
    # highlights, not actual shadow pattern geometry.
    # bg_dark may be unavailable (bg=None) on extreme-low-key images
    # because background_read can't distinguish bg from the mostly-dark
    # foreground.  Use controlled_background env hint as studio proxy.
    # Measured: short_fashion_key has mood=cinematic, shadow_den=0.972,
    #   controlled_background=True, all classifiers default to "loop".
    if mood == "cinematic" and base in ("split", "rembrandt", "loop", "butterfly"):
        _esc_cin = getattr(cr, "environmental_shadow_continuity", None) if cr else None
        _hints_cin = getattr(_esc_cin, "environment_hints", []) if _esc_cin else []
        _cin_controlled = "controlled_background" in _hints_cin
        if bg_dark or _cin_controlled:
            ls_data = getattr(cr, "light_structure", None) if cr else None
            _sd_cin = getattr(ls_data, "shadow_density", 0.0) if ls_data else 0.0
            if _sd_cin > 0.85:
                return "low_key"

    # ── Athletic rim sculpt: split-like rim from two strip lights ──────
    # Athletic/fitness rim sculpting uses two strip lights (or gridded
    # edges) from behind to carve body definition.  The face shadow
    # density is high (mostly dark) with moderate highlight width from
    # the rim wrapping around the edges.  Distinguished from rim_only
    # by wider highlights (0.25-0.35 vs <0.25) and from standard split
    # by higher shadow density (>0.3 vs <0.15 for well-lit split).
    # "rembrandt" is included because low-confidence pattern classifiers
    # sometimes return rembrandt for rim-sculpt setups where the split
    # edge is slightly asymmetric.  Guards keep regressions out:
    #   - brightness=="low" blocks split_strong (brightness=high)
    #   - 0.25 ≤ hl_w < 0.35 blocks rim_only (hl_w < 0.25) and
    #     rembrandt_classic (hl_w=0.41) / split_strong (hl_w=0.59)
    #   - controlled_background blocks mixed_light_failure (hints=[])
    #     and window_negative_fill (no controlled_background)
    if base in ("split", "rembrandt") and brightness == "low" and bg_dark:
        _esc_ars = getattr(cr, "environmental_shadow_continuity", None)
        _ars_hints = getattr(_esc_ars, "environment_hints", []) if _esc_ars else []
        if "controlled_background" in _ars_hints:
            ls_data = getattr(cr, "light_structure", None) if cr else None
            _ars_sd = getattr(ls_data, "shadow_density", 0.0) if ls_data else 0.0
            _ars_hl = getattr(ls_data, "highlight_width_ratio", 1.0) if ls_data else 1.0
            if _ars_sd > 0.3 and 0.25 <= _ars_hl < 0.35:
                # Don't override a classical pattern in extreme low-key —
                # the narrow highlight is from the turned face edge, not a
                # rim-only setup.
                if _ars_sd > 0.85 and base in ("rembrandt", "loop", "split", "short", "broad"):
                    pass  # keep the classical base
                else:
                    return "athletic_rim_sculpt"

    # ── Rim-only: face mostly in shadow, only edge/rim highlights ─────
    # Rim lighting illuminates only the edges of the subject with no
    # frontal key.  The face is predominantly dark (high shadow_density)
    # with very narrow highlights along the contour.  Distinguished from
    # standard rembrandt/split by the highlight_width_ratio being too
    # narrow for a frontal key but wider than a razor-thin editorial rim.
    #
    # Base gate: "loop" included — rim edge highlights cast an angled
    # shadow that the nose-shadow classifier reads as loop geometry.
    # Brightness gate: when classification brightness is empty (common
    # for extreme low-key images), infer "low" from shadow_density > 0.5.
    _rim_brightness_ok = brightness == "low"
    if not _rim_brightness_ok and not brightness:
        # Infer brightness from shadow density when classification is empty
        _rim_ls_check = getattr(cr, "light_structure", None) if cr else None
        _rim_sd_check = getattr(_rim_ls_check, "shadow_density", 0.0) if _rim_ls_check else 0.0
        if _rim_sd_check > 0.5:
            _rim_brightness_ok = True
    if base in ("rembrandt", "split", "loop", "low_key") and _rim_brightness_ok and bg_dark:
        ls_data = getattr(cr, "light_structure", None) if cr else None
        _rim_sd = getattr(ls_data, "shadow_density", 0.0) if ls_data else 0.0
        _rim_hl = getattr(ls_data, "highlight_width_ratio", 1.0) if ls_data else 1.0
        # For standard base patterns, require hl >= 0.15 to separate from
        # editorial_rim_key.  For low_key base, any narrow hl with high sd
        # confirms rim — the editorial_rim_key path requires sd < 0.02 so
        # there is no overlap.
        if _rim_sd > 0.45 and _rim_hl < 0.25 and (_rim_hl >= 0.15 or base == "low_key"):
            return "rim"

    # ── HURLEY TRIANGLE: clamshell + 3-catchlight detection — commented out pending cleanup ──
    # if base == "clamshell":
    #     _htri_lc = getattr(li, "light_count", 0) if li else 0
    #     _refl = getattr(cr, "reflection_architecture", None)
    #     _htri_cl = _refl.total_catchlights if _refl else 0
    #     _htri_count_ok = _htri_lc >= 3 or _htri_cl >= 3
    #     _is_high_key_mood = mood == "high_key" or (bg_bright and brightness in ("high", "very_high"))
    #     _bilateral_strong = _htri_cl >= 4 and _refl and max(
    #         _refl.per_eye_counts.get("left", 0),
    #         _refl.per_eye_counts.get("right", 0),
    #     ) >= 2
    #     if _htri_count_ok and (not _is_high_key_mood or _bilateral_strong):
    #         return "triangle"

    # ── Tabletop soft product: studio overhead product shot ───────────────
    # Studio tabletop product shots use an overhead or near-overhead softbox,
    # producing butterfly-like geometry on a small subject with no face.
    # Key discriminators vs. portrait butterfly/clamshell:
    #   1. No face detected (product shots have no human face)
    #   2. Base is butterfly or broad (overhead/near-overhead key)
    #   3. Background is even/neutral — not dramatically bright or dark
    # The mood classifier often classifies product shots as "editorial" or
    # "natural" — not high_key — since the lighting is product-optimised.
    # Guard: only fire when mood is not "high_key" (high_key product setups
    # have their own path); exclude when natural light is confirmed (window
    # products are handled separately).
    if not _face_detected and base in ("butterfly", "broad", "loop"):
        _is_product_mood = (cls.get("mood") or "").lower() in ("editorial", "natural", "commercial", "")
        _not_natural = not _env_is_natural
        _not_high_key_mood = (cls.get("mood") or "").lower() != "high_key"
        _bg_neutral = not bg_bright and not bg_dark  # neutral product background
        if _is_product_mood and _not_natural and _not_high_key_mood and _bg_neutral:
            return "tabletop_soft_product"

    # ── Tabletop soft product: dappled foliage shadows → outdoor product ──
    # Tabletop product or beauty shots outdoors or near foliage produce
    # distinctive dappled shadow patterns from leaves/branches.  The
    # "dappled_foliage" environment hint is uniquely diagnostic — no
    # controlled studio setup produces this signal across the benchmark set.
    _tsp_esc = getattr(cr, "environmental_shadow_continuity", None)
    _tsp_hints = getattr(_tsp_esc, "environment_hints", []) if _tsp_esc else []
    if "dappled_foliage" in _tsp_hints:
        return "tabletop_soft_product"

    # ── Window portrait env-agnostic fallback: gradient background ────────
    # When the env classifier misidentifies window light as studio/unknown
    # (is_natural_light=False), the combination of a side-key short base and
    # a gradient background reliably indicates window portraiture.  Window
    # light produces characteristic background fall-off (gradient from light
    # side to shadow side) that studio setups rarely reproduce.  Restrict to
    # "short" base — the key signature of side-window portraiture — to avoid
    # promoting any gradient-background shot (studio rim shots can also have
    # background gradients but typically use loop/rembrandt, not short).
    if base == "short" and bg_pat == "gradient":
        return "window_portrait"

    # ── Short fashion key: dramatic short-side lighting in studio ─────────
    # Short fashion key is short lighting (key illuminates the turned-away
    # cheek) in a controlled studio setting with cinematic/editorial/fashion
    # mood.  Distinguished from standard "short" by the intentional dramatic
    # mood and studio environment — not a window or outdoor setting.
    # Guard: strong triangle_isolation (> 0.80) means the pose resolver
    # called "short" but the shadow geometry is Rembrandt — the triangle
    # on the shadow cheek is the defining feature. Don't reclassify.
    _ls_sfk = getattr(cr, "light_structure", None) if cr else None
    _tri_sfk = getattr(_ls_sfk, "triangle_isolation", 0.0) if _ls_sfk else 0.0
    if base == "short" and not _env_is_natural and _tri_sfk < 0.80:
        if mood in ("cinematic", "editorial", "fashion", "low_key"):
            return "short_fashion_key"

    # ── Window negative fill: window light + no bounce → deep shadow side ──
    # Natural window light with no fill on the shadow side produces high
    # contrast with the lit side showing loop/rembrandt geometry and the
    # shadow side going very dark.  Distinguished from studio rembrandt by
    # the natural light source and from window_portrait by the lack of fill
    # (higher contrast, deeper shadows, mood is dramatic not soft).
    if base in ("rembrandt", "loop", "short") and _env_is_natural:
        env_type = getattr(env, "environment_type", "") if env else ""
        if env_type in ("indoor_ambient", "indoor_window"):
            ls_data = getattr(cr, "light_structure", None) if cr else None
            _wnf_asym = getattr(ls_data, "left_right_asymmetry", 0.0) if ls_data else 0.0
            _wnf_sd = getattr(ls_data, "shadow_density", 0.0) if ls_data else 0.0
            # High asymmetry + deep shadow = negative fill (no bounce)
            if _wnf_asym > 0.25 and _wnf_sd > 0.20:
                return "window_negative_fill"

    return None


